											<h2 style="text-align:left;"><?php echo $title; ?></h2>
											<p>Much has changed since Motorola first made mobile handsets accessible to the masses in 1984. Smartphones now account for over half of global mobile subscriptions and 8.4 billion connected devices are expected to be in use worldwide by the end of this year.</p>

<p>We are entering an era of ambient intelligence, where consumers are operating within increasingly smart environments that use embedded intelligence to make their lives easier. Technology is also enabling an explosion of communication over data services such as video/audio calling and chat applications, leading to a drop in traditional voice calls.</p>


<p>Connectivity is clearly king, but this isn’t bad news — far from it. With consumers increasingly using mobile as a portable gateway to the web, network operators have greater access to a valuable commodity: data. The growing influx of intelligent real-time data presents a significant opportunity for the industry to better serve users, enhance efficiency, and boost revenue.</p>

<p>But the question is: how can telecoms providers maximise the potential of their user data, while maintaining consumer privacy?</p>

<h4>Mobile Data: the opportunity and challenge</h4>
<p>Wherever we are and whatever we’re doing, it’s likely a mobile device will be nearby. In fact recent consumer research shows that more than two-thirds of UK adults own a smartphone — with usage topping 90% for 16-24 year olds.</p>
<p>When consumers interact with connected devices, they continuously ping out non-personally identifiable (PII) signals. With 66% of consumers using smartphones to go online, telcos are in an advantageous position to receive unique insight into consumer activity in both the digital sphere and the real world.</p>

<p>Once pieced together and analysed, this rich first-party data can be analysed for a variety of purposes, from segmenting audiences and enabling personalised advertising, to assessing and improving network performance. Yet this is still largely a possibility, not a reality.</p>

<p>At present, the majority of providers are not leveraging their data. For some, this is down to a lack of expertise. For others, it is because they have not yet been able to successfully blend and action multiple data sources. Privacy concerns too have played a part, with ever-stricter regulations on individual data fuelling apprehension about its use.</p>

<p>There are, however, ways for these issues and anxieties to be overcome.</p>

<h4>Unleashing the power of Mobile Data with Data Platforms</h4>

<p>The explosion in mobile data availability can make extracting useful insight difficult, especially without specialist knowledge and experience. Fortunately, there is a simple solution.</p>
<p>Providers can utilise data platforms to instantly gather multiple data sources and produce useable insight. With a detailed understanding of cross-channel consumer activity, they can map consumer journeys in the physical world across touchpoints. What’s more, they can gain vital intelligence about their competitors, which isn’t possible through first-party data but becomes an opportunity with data platforms that have location and device data amongst their datasets.</p>
<p>Mixing internal data with other insight, such as user location data, can also be used to determine where and when individuals are looking to switch networks, and how they can improve services to retain loyalty. And if the data platform is able to identify the same consumer across devices and places, telcos can link their CRM data to integrate their CRM efforts with new customer acquisition efforts, which has been a major reason for spillage to date.</p>

<p>To protect user privacy, and comply with regulations, there are several steps providers and data platforms must follow. First, it is crucial to give users the ability to opt-out of sharing their data, to ensure no information is shared with third parties. Further, providers need to comply with local data laws for all areas of operation and use strict firewalls, or encryption to keep data safe from hackers. It’s also important to never ask to access or store PII. Generally speaking, privacy policies should be designed for users of 18 years or above.</p>

<h4>How can telecoms providers put data into action?</h4>
<p>Mobile data has many applications that extend far beyond how many people are using a smartphone. For example, intelligent data analysis can establish how long consumers spend online via mobile, and when. According to our research, this is an hour and a half per day for UK users, with cellular network prime time from 7pm to 9pm and Wi-Fi at 10pm. If this overlaps with a consumer’s daily commute, it implies that they may be at their most receptive to receiving information between this time. It may also help to determine the best time to display videocontent via Wi-Fi.</p>
<p>Data can help to identify if the consumer is roaming abroad, and relevant travel plans can be pitched accordingly. They can also pinpoint which demographics use certain connection types. In our <a href="<?php echo SITE_URL; ?>allspark/telco.php" target="_blank">study</a>, 69% of 18-25 year olds preferred Wi-Fi to cellular network browsing, as did a high proportion of homemakers (64%). Telecom companies must therefore make it as easy as possible for their consumers to discover them, and latch them to their Wi-Fi sources if they want to retain them.</p>
<p>Then there are more granular insights, such as the device and connection type consumers use on certain networks. For instance, our data reveals that most Vodafone users go online with iOS devices and opt for cellular connectivity.</p>
<p>As well as packaging behavioural data to enhance its appeal for marketers — and by extension revenue — telcos can identify how and where the consumer experience can be improved. If data shows that cellular network data consumption in particular areas is high, but signal strength is low, this would indicate that providers need to invest in more towers to better serve and maintain customers.</p>
<p>Ambient data has serious potential to drive profitability and efficiency for the telecoms industry. While it’s true that, until recently, the difficulty of managing high volumes of user data has been a barrier, new developments in technology are making it possible to turn disordered data into actionable insights.</p>
<p>The biggest untapped opportunity in the telecoms sector is now finally within reach, and network providers should move quickly to harness it.</p>

