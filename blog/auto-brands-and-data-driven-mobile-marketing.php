											<h2 style="text-align:left;"><?php echo $title; ?></h2>
											<p>Mobile data analytics combined with Internet of Things is giving brands deeper insights about their customers and the ability to target them based on their preferences, lifestyle and location.</p>

<p>There has been a tremendous growth in the smartphone penetration over the last decade and this is only expected to rise predominantly in coming years.</p>

<p>According to eMarketer, smartphone users are expected to grow to 1.91 billion in 2015. This prediction is likely to materialise considering the growing availability of affordable smartphones and the 3G and 4G networks.</p>

<p>The increasing number of smartphones, and the increasing amount of time spent by users on these devices is generating a huge amount of data. This data, when analysed to generate intelligence in the form of location or audience insights, is a previously untapped opportunity for marketers to build context around marketing.</p>


<h2 style="text-align:left;">Access to predictive analytics</h2>
<p>The concept 'right time, right place and right consumer' is the recite chanted by many marketers today. Location data in the context of data driven marketing becomes essential and acts as the most powerful data point that enables us to connect a user’s online behavior with the offline context. Smartphones are the only source of location data at scale, and are unparalleled in their ability to be personal, relevant and measurable.</p>

<p>This data, combined with interest data like content consumption behavior of the user helps us sketch audience profiles of consumers. Brands with a retail presence like McDonald’s, Pizza Hut, and Woolworths are using this real time intelligence for geo-audience insights and targeting and are reaping huge benefits from this strategy. Leading marketers are also using these data insights to tie in with traditional media like out-of-home media to amplify their reach and engagement.</p>

<h2 style="text-align:left;">Present auto advertising environment</h2>

<p>The nature of campaigns by automotive brands varies according to the brand’s objectives:&nbsp; <br>
	 <ul class="listing">
			 <li>driving traffic to their events.&nbsp;</li>
			 <li>increasing footfall to the showrooms for a test drive.&nbsp;</li>
			 <li>raising awareness of their new product and its features.&nbsp;</li>
			 <li>getting insights on the preferred features in a car model or changing brand perception.&nbsp;</li>
	  </ul>
</p>
<p>For automotive brands, the auto intender audience is a far more important target when compared with all potential audiences near the showrooms.</p>

<p style="text-align:center"><img src="<?php IMAGE('blog/journey-of-car-buyer.jpg'); ?>" style="width: 100%;" alt="Auto Brands inforgrapic" /></p>

<p>Brands can also create their customised audiences using location data to cater to various categories within the brand, like luxury, premium and standard models. Historical location data and content consumption behavior enables brands to build context around a consumer’s level of affluence and their affinity to cars. Location and audience insights help fine-tune the brand messaging for service offerings, product launches, and event marketing. </p>

<p>The ability of auto brands to bring in relevance to these mobile audiences creates meaningful brand experiences. For example, Audi ran a mobile campaign to communicate its Audi A8 features and also to evoke purchase interest in them. A special gyroscope creative gave the smartphone and tablet users a 360-degree view of the car interior. The Audi audiences were also given an option to watch the Audi A8 video.</p>

<p>As a result of the campaign, it was found that video was the preferred source of content consumption with around 11% engagement. Navigation, Interior and MMI Touch were the top three features explored by users, conversely the B&O Sound and Head-Up display features were not quite popular.</p>
<p>Users aged between 25-49 years were seen more engaged than the others. This data provides rich insights to Audi and has already been used in multiple ways to enhance their relevance. Further to insights and targeting, location data can also help measure attribution from the campaign, whether that be into dealerships or a digital visit.</p>

<p>A Fortune 500 auto brand ran a campaign to target auto intenders for a promotion and to drive dealership traffic. A custom audience was created for the brand by accumulating unique devices that had visited the dealership of the client or its competitors in the past. The campaign then targeted these custom audiences when they were close to the client dealerships. A 23% foot traffic increase was seen during the campaign, with the maximum increase happening within 200m radius from the outlets.</p>
<p>The brand could also track and compare engagement around its own and competitor outlets. The attribution measurement is possible for any type of campaign, traditional or digital, and brings in measurability in advertising like never before.</p>

<p>Evolution of Internet of Things The evolution of intelligent cars is taking an interesting approach. Most of the white-labeled consumer goods and home appliances would be running on integrated platforms and cars are no different. All cars these days have built-in GPS systems, and some of them can be connected to the owners mobile through apps. The owners in turn get access to information about their car and are able to connect with the customer service at ease to go in-depth on any issues being faced by them.</p>

<p>With more real time data from cars in terms of source, destination, driving habits etc., another layer of context added to audience segments would make it highly targeted and more effective for brands. Auto marketers can now take control of their target audience and should keep in mind the following three things to maximize the return on their marketing spends: &nbsp;<br>
<ul class="listing">
			 <li><b>1. Understand their audience:</b> Creating customised audience based on your product and studying them using data insights can help you be relevant.&nbsp;</li>
			 <li><b>2. Relevant targeting:</b> Reaching out to the right audience is the first step in personalization. Brands also need to pay attention to the messaging and interactivity of the campaign.&nbsp;</li>
			 <li><b>3. Leveraging mobile and measurability:</b> Mobile needs to be seen not just as another media channel but as means to connect the online and offline media consumption of consumers. It brings measurability and can be seen as an attribution source across media.&nbsp;</li>
</ul>
</p>
