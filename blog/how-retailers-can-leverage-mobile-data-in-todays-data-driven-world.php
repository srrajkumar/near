											<p>As the retail landscape evolves, it’s only natural that brands are shifting their focus away from the traditional online platforms. Increasingly, retailers are casting their gaze at the endless opportunities provided by mobile retail.</p>

<p>The reasons are simple: More people are using their internet-connected smartphones to shop online. According to a recent comScore report, mobile devices account for as much as 60% of the time spent browsing retail sites. That trend is up sharply from 2013, when less than half of retail site visits originated from tablets or smartphones.</p>

<p>A recent study by Deloitte Digital estimates that as much as 20% of all offline retail sales will be at least partially influenced by customers using mobile devices. These customers may use their mobile devices to carry out comparison checks, read product reviews, or check in-store product availability and deals.</p>

<p>In this data-first age, consumers want the freedom to shop anywhere, at any given time. To thrive in this environment, retailers must adopt a truly mobile mindset. Mobile devices have changed the relationship between brand and customer, creating a much more flexible and diverse shopping experience. The old boundaries between online and brick-and-mortar retail aren’t as sharply drawn as they once were, and may soon disappear altogether.</p>

<p>A Deliotte Digital study published in 2014 found that 63% of UK and German shoppers used multiple retail channels before spending at least £100, with 25% of frequent shoppers using mobile websites while browsing in the physical retail location. The study also revealed that even infrequent shoppers used mobile devices to research products 11% of the time whilst at bricks-and-mortar locations.</p>

<p>From an ecommerce perspective, this shift towards mobile also provides an opportunity to make use of the growing pool of mobile data. Traditionally, ecommerce retailers have relied on limited metrics like CRM, spend size, transaction history and other first-party details. With mobile, data like location, audiences, content searches, and shopper behaviour become available through third-party data providers.</p>


<p>This mobile data enables brands to market to their consumers on a real-time basis, using tools like personalised and customised ads. Location-based audience data helps brands to identify their potential customers, and content consumption data can allow for more complex models of consumer preferences and behaviour over time. This data is a goldmine for retail marketers.</p>

<p>Using this technology, a business executive and a fashion-obsessed teenager browsing the web on their phones while sitting next to each other at a bus stop could both see personalised content for clothing stores. One would see ads for a high-end professional attire store, the other for a store selling the latest trends. This level of personalised targeting was impossible only a few years ago on mobile devices.</p>

<p>The more brands and marketers can make use of this data in the mobile age, and build their cross-channel retail practices around it, the more likely it will be for shoppers to see their advertising, consume their content, and purchase their products.</p>

<p>It’s important that retailers use common sense and moderation in these programs. However, some customers may object to location tracking, making it important for the advertiser to offer opt-out tools. The goal is to increase customer engagement, and to fine-tune to the brand’s overall mobile and cross-channel campaigns.</p>

<p>By combining data sources, and by taking advantage of mobile data and insights, brands can see powerful results. Mobile insights & targeting can be very useful tools for attracting new customers, with some brands seeing a 25% growth in sales from new customers. Retailers can even use this data to compare customer behaviour with competitor stores, or simply to enhance their consumer loyalty.</p>

<p>Mobile data has never been more important for retailers. Online retailers can benefit from mobile insights, like the location and real-world interests of their shoppers. Offline retailers can see a range of benefits by using mobile data to shed new light in their in-store shoppers’ buying behaviour.</p>

<p>We live in a data-first age, where even the tiniest insights into consumers can yield huge results for both online and offline retailers. Consequently, all retailers should access available data, and learn to use it in the most effective way. To see the best results, mobile data should become an essential part of their marketing strategy.</p>

