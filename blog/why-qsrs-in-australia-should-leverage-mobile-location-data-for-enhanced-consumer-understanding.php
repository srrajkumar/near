											<h2 style="text-align:left;"><?php echo $title; ?></h2>
											<p>In Australia, the Quick Service Restaurant (QSR) industry will see an annual revenue growth rate of <a href="http://www.ibisworld.com.au/industry/default.aspx?indid=2005" target="_blank">2.4 percent</a> in the next five years. This market, which is led by McDonald’s and Subway has an annual revenue of more than $15 billion currently and employs more than 150,000 people. The reason for this growth and massive amount of revenue is simple: in today’s fast-paced world, consumers are seeking more ways to save time. Eating at a QSR is also cheaper than eating at more formal restaurants and is child-friendly, which is great for those with families.</p>

<p>As more people eat at QSRs, these restaurants will want to step up their marketing and business strategies. One area that many should seriously consider pursuing is mobile location intelligence.  Location Intelligence gathers data, such as a customer’s geographic location and demographics and analyzes it to provide an actionable insight into the customer’s actions. This is especially important for Australian businesses because the country is <a href="http://www.emarketer.com/Article/Asia-Pacific-Boasts-More-Than-1-Billion-Smartphone-Users/1012984" target="_blank">one of the leaders</a> in mobile phone usage. </p>

<h2 style="text-align:left; font-size:18px;">What Location Intelligence can tell Businesses about Consumers</h2>
<p>The intelligence gained from location data can provide QSRs with a large amount of information about their consumers. This includes insights like which places they tend to visit, what do they do in the digital world, what type of consumers they are (affluent, homemaker, etc.), where they’ve been, what they’ve been doing, and much more. This information can be used to determine the types of strategies that could influence the consumer, the peak times for store visitation for different brands, and why customers act the way they do.</p>

<p>With this information, it’s possible to see more than just what the consumer did when they entered the restaurant.  Brands have access to the consumer’s entire journey. What did they do before eating at a QSR? Have they eaten there before? What type of consumer are they, and is that particular type of consumer typical? </p>
<p>QSRs can evaluate their business strategies to determine if they are reaching the desired audience and learn which locations are more likely to bring in what type of consumer. </p>

<p>In the case of McDonald’s and Subway, the two restaurants could gain very actionable insights from using location intelligence. For example, when it came to their locations in Perth, McDonald’s might be surprised to learn that more consumers preferred Subway. Their store on Hay Street received 21% of traffic out of the total users analysed in Perth during the <a href="https://near.co/media/pdf/consumer-intelligence/mcdonalds-and-subway-consumer-insights-for-australia.pdf" target="_blank">Near study</a> in December 2015.  With the Subway on Forrest Place taking 79% of the consumers, McDonald’s would be faced with a decision: let Subway continue to dominate this particular market or open a new location in Perth that may be more convenient to consumers.  Another option would be to try changing its business tactics or consider tailoring its offerings more towards the needs of consumers in the city.</p>


<p>Near’s study looked at mobile location data from five different locations across Australia to see what insight could be drawn about consumers who ate at McDonald’s and Subway.  The locations included one of each restaurant in Perth, Adelaide, Melbourne, Sydney, and Brisbane.  This study was done to understand how consumers’ footfall behavior can vary within cities and also to glean insight into their tastes and preference. </p>

<p>From the study, one such piece of information that both restaurants could easily take advantage of was ‘when’ do consumers walk in these restaurants. Consumers who ate at McDonald’s did so in the early evening hours, while those who ate at Subway did so later in the evening.  This information could be used by both restaurants to fuel strategies aimed at drawing consumers away from the competitor.  For example, McDonald’s could introduce later hours or a special late night menu, while Subway could promote a late afternoon/early evening special.</p>

<h2 style="text-align:left; font-size:18px;">Knowing the who’s &amp; their whereabouts that impacts business decisions</h2>

<p>Another piece of information both McDonald’s and Subway would be interested in learning is who their audience is.  The majority of those who eat at QSRs, according to the Near study, are from the professional and affluent categories.  In fact, there were more professionals eating at McDonald’s and Subway than any other category, although affluent consumers were a close second in most cities (and actually made up a higher percentage of diners at McDonald’s in Perth). This tells QSRs (not just McDonald’s and Subway) that professionals tend to eat more fast food and that they should aim their strategies towards this group.  </p>
<h2 style="text-align:left; font-size:18px;">The bottom line</h2>

<p>The insights that can be derived from using mobile location data can be used by brands in a number of ways.  From deriving consumer habits to looking at who is eating where and when, QSRs will be able to better measure how successful their marketing and business strategies are and will be able to better focus them on specific consumer groups. In the long run, this will allow businesses to improve sales and dominate the market.</p>

