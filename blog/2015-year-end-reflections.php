											<p>As we draw towards the end of 2015 and take a look back, the year has been one of the most fruitful and professionally satisfying. We witnessed 300% growth in revenues through our platform and scaled up externally, as well as internally.</p>

<p>The analysts have forecasted the Global LBS platform market to grow at a CAGR of 22.42% over the period 2015-2019. This market includes various location-based use cases such as marketing, search, indoor positioning, and analytics. We are very proud to be amongst the <a href="http://www.researchandmarkets.com/publication/m9z3i7i/global_lbs_platform_market_20152019" target="_blank">six key players</a>, from around the globe in this space.</p>

<p>Earlier this year, Gartner recognized us as the <a href="https://www.gartner.com/doc/3038620/cool-vendors-consumer-dynamics-" target="_blank">Cool Vendor</a> in Consumer Dynamics. This recognition highlights companies that are providing innovative approaches to digital businesses for understanding why consumers do what they do, for consumer segmentation, and for building digital relationships. We were amongst the five players chosen from around the globe for this recognition. </p>


