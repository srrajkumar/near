<p>Despite the significant growth in our team, we have been able to successfully maintain our culture across all six offices. Furthermore, we have been uncompromising in our policy to hire only superstars in all positions. Though we had amazing tech talent join us in 2015, we are always actively looking out for tech superstars to build out the next generation of our systems to cope with the scale we are aiming for the coming year.</p>


<p>In 2015, we have seen 10x growth in the data that we store and analyze. We are adding 100 Mn new users every month and are processing over 3 Mn events per second through our pipelines, and this continues to grow exponentially. Partnerships like Google, Experian, Telstra, Roy Morgan, and oOh! Media have led us to work on high-end data systems, and has increased our urgency to hire exceptional talent skilled in working on our cutting edge technology.</p>


<p>Another product offering with sizeable traction this year was our consumer insights piece. We saw a lot of brands commissioning and utilizing our mobile data insights for business, and not just marketing intelligence. Our notable consumer dynamics driven research efforts in 2015 included <a href="<?php PDF('consumer-intelligence/shopper-insights-black-friday-near-research.pdf'); ?>" target="_blank">Black Friday</a> shopper studies, <a href="<?php PDF('consumer-intelligence/the-rugby-world-cup-2015-infographic.pdf'); ?>" target="_blank">Rugby World Cup</a> study on sports audiences, and a study on Australian <a href="<?php PDF('consumer-intelligence/australia-students-research.pdf'); ?>" target="_blank">university students</a> in the higher education category. </p>

<p>New offices, new hires and strong growth have defined 2015 for us. As we end this year on a high note, we are very excited about our offerings in 2016. Wishing all of you a very happy and prosperous New Year!</p>


