											<h2 style="text-align:left;"><?php echo $title; ?> </h2>
											<p>The adoption of mobile as a marketing tool is increasing across verticals, particularly within the Quick Service Restaurant (QSR) industry. As “out of home” food and drink consumption grows, presence in areas with high population density such as malls and business districts becomes vital.</p>

<p>A defining feature of this category would be its impulsive nature, given that food choices are unplanned more often than not. As such, it is important to attract customers through timely reminders that allow for heightened “top of mind” awareness and are location aware. Luckily for marketers, a wealth of insightful information is right within their reach.</p>

<p>By tapping into mobile devices that are always at hand, marketers are now privy to a powerful source of real-time access to location data.</p>

<p>Location data helps brands derive audience data at scale and enables them to create audience segments to target. With mobile data, QSRs can now understand their consumers at a granular level.</p>

<p>They can obtain powerful insights into the preferences and interests of the consumers visiting their stores in the real world. They can then compare this with the consumers in the competitor’s stores and understand the differences in consumer behavior that cause individuals to prefer one store over another.</p>

<p>With this knowledge in hand, QSRs need to instill a systematic approach that draws customers to their establishment, encouraging loyalty and appreciation towards the brand.</p>


<p>Creating brand loyalty is fundamental for success within this industry as that is what differentiates brands from competitors. Especially in this new world, where taste, behavior and attitude towards food and beverage is changing, and brands are seeing a rise consumer demands, brands have their work cut out for them.</p>

<p>Location-intelligence companies are able to leverage on this valuable data, giving marketers the opportunity for the last-mile influence in real-time.</p>


<p>Recently, Near did an extensive <a href="http://near.co/news/qsrs-or-coffee-chains-near-latest-research-reveals-consumer-trends-seen-across-south-east-asia.php" target="_blank">research</a> in Southeast Asia studying various QSRs and Coffee Chains to understand the preferences, profiles and frequency of visits among various brands of QSRs and coffee chains.</p>

<p>Using anonymous data gathered from mobile devices, we were able to accurately gauge many kinds of audiences and profiles of consumers not just basics like demographic profiles  but locational behavior i.e. where a person has been or will go, being completely personally identifiable information (PII) compliant.</p>


<p>For this research we looked at defining audience segments across a certain brand set, we looked into brands such as McDonald’s, Pizza Hut, Starbucks and Coffee Bean across six countries.</p>

<p>The research revealed that the inclination towards Coffee Chains and QSRs are highly correlated across the different genders in each country. Coffee Chains were favoured by people in the 16-25 years age group, consistently across the region.</p>


<p>Frequency of visits also showed some clear patterns emerging. As suggested by the study, in Singapore, QSRs had as many repeat customers as Coffee Chains with 38% of the respondents visiting QSRs and 37% visiting Coffee Chains more than once a week.</p>
<p>While Coffee Chains enjoy a steady stream of traffic in late afternoons and early evenings between 4pm-7ppm, QSRs tended to have high footfall during early and late evenings between 5pm to 10pm. Time poor segments such as professionals and students made up most of the visitors in this time band, coinciding with dinner time.</p>

<h4>Make the most of collected location data</h4>
<p>Understanding preferences and behaviours of the brand’s audiences that are locationally aware, of existing and potential customers provides brands actionable intelligence. This in turn leads to better decision making and developing a cohesive, effective marketing strategy. For example:</p>
<ul class="listing">
	<li>Finding – Starbucks saw the highest footfall in Indonesia, followed by Hong Kong and Thailand  
		<ul class="listing">
		<li>Actionable intelligence – Outlets could tailor their offerings to specifically attract these particular markets</li>
		</ul></li>
	<li>Finding – In Singapore, among the demographics analysed, Homemakers exhibited the highest footfall to Starbucks  
		<ul class="listing">
		<li>Actionable intelligence – Starbucks may want to roll out campaigns and promotions specifically aimed at this demographic such pampering sessions for mums at home</li>
		</ul></li>	
	<li>Finding – Apart from Thailand, all countries analysed in the study revealed that mobile-device using customers of Starbucks were users of Android 
		<ul class="listing">
		<li>Actionable intelligence – Outlets in these countries could tweak their apps and mobile promotions to ensure they are Android-friendly, so they can tap into the vast majority of their mobile-savvy consumers</li>
		</ul></li>
	<li>Finding – In business districts, people are more likely to visit Coffee Chains rather than QSRs 
		<ul class="listing">
		<li>Actionable intelligence – A QSR brand such as McDonald’s may look into providing varied menu options or promotions through the day to attract footfall away from Coffee Chains in these locations</li>
		</ul></li>	
	<li>Finding – Different countries saw footfall traffic peaking at different timings
		<ul class="listing">
		<li>Actionable intelligence – For example, in Starbucks Thailand, peak hour was found to be from 3pm-6pm. With this knowledge, outlets there may offer promotions or menu options around late-afternoon snacks</li>
		</ul></li>	
	<li>Finding – Among the demographics analysed, the Affluent and Homemakers tend to frequent Coffee Chains between 2pm-5pm  
		<ul class="listing">
		<li>Actionable intelligence – Coffee Chains can consider customising their offerings to attract these particular demographics via themed promotions or campaigns</li>
		</ul></li>
	<li>Finding – QSRs see most traffic between between 4pm-8pm 
		<ul class="listing">
		<li>Actionable intelligence – A Subway outlet in a competitive environment could target potential customers in and around Pizza Hut and McDonald’s locations during these times</li>
		</ul></li>	
</ul>
<p>At a broader level, relevant consumer insights also help brands to identify new markets and locations with higher potential, based on the population segments they cater to.</p>

<h4>Conclusion</h4>
<p>QSRs need to keep pace with relevant consumer trends and fine-tune their marketing and media strategies to implement successful campaigns. Marketers should definitely consider leveraging the data that mobile has to offer, of which location related data forms a subset, to stay relevant for their audiences. Armed with such insight, QSRs can further tailor their offerings to attract attention from those profiles they have yet to target, and also make broader business decisions.</p>
<p>It is evident that location intelligence becomes an essential asset in determining their next step, marketing strategies and customer outreach. Location intelligence has proved to be an indispensable asset to brands, offering them a deeper look into the minds and behaviour of audience.</p>
