											<p>
Today's consumers are a noisy group when it comes to data. Every time they visit a website, browse an e-commerce storefront, click on an ad, or even like or follow a brand on their favourite social media network, they leave a trail of data. Individually, these pieces of data don't mean very much, but when combined and analysed, they can yield a huge amount of information about a consumerâ€™s buying intent.</p>

<p>More than <a href="http://www.emarketer.com/Article/UK-Adults-Spend-More-Time-on-Mobile-Devices-than-on-PCs/1012356" target="_blank"> ever </a> before, consumers are accessing online content through their mobile devices. The old methods of tracking consumer intent in the digital world, which assume the consumer is using a desktop computer and browsing from a location-bound IP addresses, aren't the right model for understanding the modern shopper's journey.</p>

<p>Today's shoppers take multiple paths on their route to purchase. They may discover a product on social media, read product reviews on multiple retail sites, and they may compare prices and discounts on a variety of platforms. After all that, they may still decide to purchase the item in a physical location. To influence these shoppers' behaviour, you need the best data available.</p>

<p>Brands can even use their own data (spending data, retail store footfall data, merchandising response rates, promotional marketing traffic) to explore a range of trends and customer behaviour that was previously invisible before the age of high-quality mobile data. By making use of that data, such as where customers live, work, eat and holiday, it becomes possible to create more targeted segments and marketing strategies.</p>

<p>Mobile data is important for enabling marketers to keep up to date on how consumers discover, research and purchase products as they shift towards a mostly mobile shopping experience.</p>

<p>This brings us to the true power of mobile data: determining consumer intent. By comparison to the relatively limited information available through traditional user data, mobile data is more personal, insightful and measurable. For instance, mobile data can provide highly accurate time and location information, allowing marketers to track where and when consumers were when they saw a specific ad campaign, browsed a specific landing page or purchased an item.</p>

<p>Are your shoppers browsing sites on their morning commutes? Are they more likely to make a purchase on their lunch breaks, or after work? Are they comparison shopping on your site while browsing a competitor's physical storefront? Are they browsing product reviews before making up their mind to buy? These are all questions mobile data can answer and it's just the tip of the iceberg when it comes to determining user intent.</p>

<p>Search data, social data, content consumption data, location data, audience data, cross-screen data, point-of-sale (POS) data and countless other types of information allow today's marketers to gain previously impossible levels of insight into the interests, shopping patterns and buying intent of the consumer. Mobile data helps marketers, retailer and advertisers connect the dots along every step of the consumer's journey.</p>

<p>Another important element to mobile data is measurability. Traditional third-party data sources can only offer rough insights about the performance of your digital marketing, such as traffic growth. Mobile data, on the other hand, can deliver footfall data from actual visits to a physical location. Tied to supplemental advertising, like billboard advertising, QR codes or app-based promotions; a wealth of previously invisible data becomes available. It can also generate powerful insights on the kind of consumers that walked into a particular store at a particular time, allowing marketers to identify whether an increase or decrease in footfalls came from a specific consumer segment. This information is very useful to marketers looking to analyse consumer behaviour in real time, and take measures accordingly.</p>

<p>Here's the takeaway: Mobile data is not only important and relevant; it's rapidly becoming indispensable as an online shopping and marketing tool. If analysed properly and at a sufficient scale, it can show new pathways of consumer behaviour and highlight consumer intent. It enables marketers to bring new and relevant brand experiences to consumers, and allows for new levels of measurement at all stages of the shopper's journey. If you aren't using mobile data yet, you're missing out.</p>
