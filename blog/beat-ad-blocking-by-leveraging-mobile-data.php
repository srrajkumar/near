											<h2 style="text-align:left;"><?php echo $title; ?> </h2>
											<p><b>Is blocking the ad-blockers the answer to the on-going ad-blocking battle? Ken Parnham, our General Manager for Europe argues for applying valuable insights into consumer behaviour (which can be gained from looking at mobile data) for winning back consumer trust and delivering relevant content that makes ad-blocking unnecessary.</b></p>

<p>As we approach the end of 2015, the issue of ad blocking remains top of the agenda for marketers. But what can the industry do to resolve the issue? For some digital publishers, the solution lies in blocking readers who block their ads — City A.M. recently became the first UK newspaper to trial this tactic. Yet such approaches are unlikely to result in a long-term or mutually beneficial truce.</p>

<p>Instead, the industry must focus on delivering greater value for consumers by ensuring content is perfectly matched to their needs, and mobile data provides marketers with exactly the right insight to do so. Offering a detailed overview of consumer habits and activities, mobile data can be used to deliver effective, personalised content to consumers at the most opportune time and place — making it a key weapon in the battle against ad blocking.</p>

<p>Mobile data is already facilitating better audience understanding and becoming coveted currency across the digital marketing industry as a result. For example, Near recently analysed mobile location data to provide marketers with useful consumer insights at five of the UK’s most popular shopping locations, including consumers’ age, gender and preferred shopping days. Marketers were able to use these actionable insights to better understand their target audience ahead of the Black Friday and Cyber Monday weekend.</p>

<p>So, the question is: Why are consumers blocking ads, and how can marketers leverage mobile data intelligence to create ads with which they will want to engage?</p>

<h4>Helping brands to win back audience trust</h4>
<p>Consumers do not appear to object to advertising, with 71% of ad block users open to being served ads that meet acceptable criteria or are whitelisted. It is ads considered to be visual clutter, due to poor quality and irrelevance (cited by 64% of users), that drive consumers to block ads on mobile. Effective use of mobile data, particularly mobile location data, can help address this issue.</p>


<p>Data gathered from mobile usage, such as search, cross-screen, location and point-of-sale (POS), offers insights into consumer interests and habits that can reveal surprising trends, such as younger shoppers preferring to visit stores associated with more mature audiences. With accurate data as a foundation, marketers can improve the efficiency, accuracy, and relevance of campaigns — delivering tailored content for the right individual, at the most appropriate time.</p>

<h4>Delivering personalised content in real-time</h4>
<p>Speed is essential to capture consumer attention in the fast-paced digital world, yet marketers must engage their audience at the right time, which is not necessarily as soon as they look at their smartphone. By understanding the movements of consumers, mobile location data can highlight the best times to serve marketing messages, without being interruptive.</p>


<p>For brand marketers, this insight could drastically reduce the wastage of ads received at times when they are unlikely to be effective and boost engagement with ads delivered at exactly the right moment. For instance, mobile location data that shows consumers frequently topping up their shop mid-week could be extremely useful to food and drinks brands keen to optimise sales of seasonal produce in the run-up to Christmas.</p>

<h4>Bridging the online and offline consumer journey</h4>

<p>Although it may not always be the device on which the final sale is made, the role mobile plays in the path to purchase makes consumers easier to follow across other channels. Once a consumer is identified via mobile data, their interactions can be traced online and offline, creating a unified picture of one individual journey.</p>


<p>There is no doubt that mobile data should, therefore, be a significant part of both in-store and online campaigns, yet this is only one of its many applications. While it facilitates marketing directly to mobile devices (such as serving targeted messages to consumers in close proximity to a competitor’s store) insights drawn from mobile usage can also form the basis of a successful cross-device marketing strategy.</p>

<p>Knowledge of the location of a consumer, their inclination to purchase, and their habitual activity can inform relevant, engaging marketing that transcends channels and devices. Mobile location data that shows a commuter regularly uses their smartphone on the way home and switches to their tablet once they arrive will provide a brand with the necessary knowledge to serve tailored cross-device messages that complement each other and reach the recipient at the optimal time.</p>


<p>With the ‘adpocolypse’ narrative rumbling on, marketers need to reflect on how, in the year ahead, the ad blocking battle can be won. By leveraging the growing pool of mobile data, brands can utilise actionable consumer insights to tailor campaigns across channels and ensure consumers are delivered compelling, relevant and timely content that they won’t want to block.</p>
