											<h2 style="text-align:left;"><?php echo $title; ?></h2>
                                            <p>Christening your baby as an entrepreneur can sometimes be a daunting task. Especially these days, when most industries are crowded with numerous startups. The name that you wanted is already taken. But the bigger challenges are:<br>
	 <ul class="listing bullet-style">
			 <li>Is the name unique? </li>
			 <li>Does it easily relate to what you do?</li>
			 <li>Can it become a global brand?</li>
			 <li>Is it meaningful, so others will care?</li>
             <li>And, is it cool enough?</li>
	  </ul></p>

<p>Many startups do not spend a lot of time on the new name, beyond articulating which domain name is available. And sometimes it is difficult to see that long-term vision of where you want to be, when you are starting up in a garage with your trusted friend.</p>
<div class="brick">
<p>There is nothing wrong in getting started quick and then revising your brand strategy. Blue Ribbon Sports did not have the resources, nor the far sight of becoming the global brand that it is today; Nike.<br>
  <img style="max-width:403px;" class="fluid-width" src="<?php IMAGE('blog/nike.gif'); ?>" alt="Nike"/> </p>
</div>
<p>So you get started, grow, and then grow a lot more. And then realize you either missed to address one of the above points, or you no longer are doing what you originally ventured out to do.</p>
<div class="brick">
<p>Time to rebrand! But if rebranding is not done right, it could backfire badly. Gap had to do a U-turn within two weeks of rebranding.<br>
  <img style="max-width:501px;" class="fluid-width" src="<?php IMAGE('blog/gap.gif'); ?>" alt="GAP"/> </p>
</div>
<p>Your rebranding strategy has to be carefully thought through. Here are a few key lessons we learnt:</p>
<h2>“Rebranding is all about <br>staying relevant” </h2>
<ul class="listing bullet-red">
<li>Stay true to your positioning:</li>
</ul>
<p style="margin-left:10px;">Successful startups are agile and stay ahead of the competition, if any. This also means pivoting multiple times in your life before you find the winning position. Your brand has to stay true to this.</p>

<div class="brick">
<p>When we decided to drop the ‘Ad’ in AdNear it was because we wanted to stay true, and be relevant to what we do today. We found our winning position.<br>
  <img style="max-width:495px;" class="fluid-width" src="<?php IMAGE('blog/near.gif'); ?>" alt="NEAR"/> </p>
</div>
<ul class="listing bullet-red">
<li>Take your customers along:</li>
</ul>
<p style="margin-left:10px;">You don’t want your customers to feel disconnected from the new brand. Talk to them well ahead of your rebranding exercise. Explain why you are doing the rebrand and how it is going to affect them.</p>
<ul class="listing bullet-red">
<li>Walk the talk:</li>
</ul>
<p style="margin-left:10px;">Rebranding is not just about changing your logo. Your brand encompasses your customer perception and the experience of working with your products and team amongst other things. Rebranding has to reflect in all of these. </p>
<ul class="listing bullet-red">
<li>Get a dedicated team in place:</li>
</ul>
<p style="margin-left:10px;">The rebranding exercise requires meticulous planning. From doing research well ahead of the rebrand, to defining your new brand identity, to encompassing the new look and feel in your website, marketing collaterals and all external touch points to the big launch. It’s well worth getting a smart team in place to manage this.</p>
<ul class="listing bullet-red">
<li>Think big and long-term:</li>
</ul>
<p style="margin-left:10px;">It’s generally not a good practice to rebrand too often. Which means you need to think long-term. Where is your brand going to be in the next 5 or even 10 years. Think big!</p>
