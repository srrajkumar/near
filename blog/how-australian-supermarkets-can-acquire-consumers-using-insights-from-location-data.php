											<p>With just four grocery brands accounting for an <a href="http://www.ibisworld.com.au/industry/default.aspx?indid=1834" target="_blank">estimated 90.8% of the Australian supermarket industry</a>, it's safe to say that competition for getting customers is fierce. With an expected growth of 2.5% over the next few years, expected to bring total industry revenues in the country to around $90 billion, there's a lot at stake. The relatively recent appearance and growing popularity of German-owned discount-focused chain ALDI has forced the remaining three market leaders — Woolworths, Coles and IGA — to take even the tiniest customer trends into account. </p>

<p>Location insights today have become necessary to add the geographical dimension to consumer trends and patterns. Making sense of location data to derive actionable consumer insights at scale and then reaching consumer in real-time is the smartest way to drive their marketing strategies and business operations. The real-time insights generated from the data & its analytics help brands get closer to its consumers by aligning their needs to match consumer behavior at the most relevant time and place.</p>

<p><a href="https://near.co/" target="_blank">Near</a> recently conducted a <a href="https://near.co/pdf/consumer-intelligence/australia-supermarkets-research.pdf" target="_blank">study</a> using mobile location data in Australia's two largest cities — Sydney and Melbourne — for the three largest grocery chains*. Not included in this study is data from ALDI, as it currently holds a relatively small market share. The study was done in 3 locations per city- Darlinghurst, Double Bay and Bondi Junction in Sydney and Toorak Road, South Yarra and Prahran in Melbourne.</p>


<p>In terms of overall population size, Sydney (4.84 million) and Melbourne (4.44 million) are fairly similar. However, footfall seen via mobile data in Sydney (73%) dwarfed that of Melbourne (27%) across supermarkets. This definitely indicates higher number of users active on mobile in Sydney within the supermarkets.</p>


<p>Supermarket traffic in Melbourne was predominantly composed of 16-24 year olds and those over 36, while the 25-35 demographic was dominant in Sydney. In both cities, shoppers greatly favored the use of iOS devices over Android.</p>


<p>IGA supermarkets had the greatest percentage of footfall traffic seen via mobile data in both cities, accounting for well over 40% of total footfall in each. Coles was strongly favored in Sydney, while Woolworths proved to be more popular in Melbourne.</p>


<p>Peak shopping times also varied strongly between the two cities, with Saturday afternoons and early evenings being the most popular for shoppers in Melbourne. In Sydney, however, shoppers were frequently seen on Monday during early evenings.</p>

<p>In both cities, the majority of shoppers were males. In Sydney, male shoppers generated 64% of foot traffic, while Melbourne saw men accounting for 70% of visits. </p>

<p>While that general trend for gender distribution holds across all three chains, some notable variation exists. In Sydney, for instance, upwards of 72% of all Coles shoppers were male. At the same time, male IGA shoppers account for only 59% of footfall, providing the highest percentage of female visits at 41%.</p>
<p>In Melbourne, however, males make up 76% of IGA footfall. Coles had the highest percentage of female visitors in the city (36%), although Woolworths wasn't far behind (32%). Interestingly the male-to-female ratio at Woolworths was effectively identical in both cities.</p>
<p>Apple's iOS has been on the rise in Australia, <a href="http://www.businessinsider.com.au/apple-android-market-share-australia-2015-3" target="_blank">growing from a 35% market share in 2014 to 42% in 2015</a>. One might expect that this overall percentage would hold in terms of supermarket footfall traffic, but it doesn't. In fact, in terms of popularity among supermarket shoppers, iOS devices are dominating their Android competitors in both cities.</p>
<p>Roughly 60% of smartphone users at supermarkets in Sydney were iOS users, and an astonishing 66% of shoppers in Melbourne used iOS devices. Woolworths in both cities saw iOS rates over 65%.</p>
<p>Android devices held only a narrow majority in a single location — Coles stores in Sydney — and even then with a relatively small lead of 52%. By contrast, 71% of smartphone users observed in Melbourne's IGA chain were iOS users.</p>
<p>The shoppers were found on gaming and social apps, with music apps also widely represented.</p>
<p>With location data accessible at scale, store wise insights can be used by brands to make decisions that could lift sales. For example, the age and gender insights could be crucial to decide the merchandising mix at store level.</p>
<p>If IGA is seen to have active in-store mobile users, it is a great opportunity for the brand to target consumers over mobile. Coles and Woolworths may need to look at targeting their audiences outside their stores, since their ‘moment of receptivity’ is clearly before they enter the store. This study, though limited in the number of stores and cities it covers, is very effective in showcasing the potential location data has for brands. </p>

<p>From the perspective of a footfall-dependent business, this study provides vital insights about reaching the target consumers in the two studied cities. Brands could use this data & analytics across their own and competitor stores to know the kind of consumers walking in to each store, and decide their merchandising, their business marketing, strategies and operations. In-store engagement can also be planned effectively if the brands know the preferences of their consumers. With the competition growing fierce by the day, using data for intelligent business decisions will make all the difference.</p>

<p>As Published in <a href="http://www.bandt.com.au/newsletter/how-aussie-supermarkets-can-acquire-new-consumers-using-location-data">B&amp;T</a> </p>

<p style="font-size:12px;">*The research conducted by Near included analysis of IGA, Coles and Woolworths in Sydney and Melbourne only. Therefore, the trends may differ in other Australian cities for the stores. The study analysed the location data to understand the consumer behaviour in and around these stores for the month of October 2015. </p>


