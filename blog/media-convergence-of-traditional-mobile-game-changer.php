		<p>A few days  back, I was at Changi Airport to catch a flight and something interesting  caught my eye. There was this guy staring at a large billboard of a luxury  brand and punching something on his smartphone. He&rsquo;d look up, and then go back  to his handheld. I couldn&rsquo;t resist from finding out more. It turned out that  the brand was encouraging people to download their app and/or call their  support center for more information. </p>
        <p>This is an  interesting example of a user wanting to engage with a brand after getting  exposed to an ad (exactly why the ad was there in the first place). The  challenge here though is the transition from the offline to the online channel.  And this problem gets further complicated as users start to spend more time on  their smartphones and tablets to &ldquo;find&rdquo; or &ldquo;do&rdquo; more. If the brands could  reinforce their messaging onto the handheld devices at the &ldquo;right&rdquo; time, it  would make the follow up seamless and hence impact conversion and measurement. </p>
        <p>Traditional media  channels have been an integral part of consumption behaviour and will continue  to be. No surprise then that they have been the primary channels for brand  outreach. However, as the <em>Millward Brown  AdReaction Study 2014 </em>indicates, in most of the Asian markets like Japan,  Australia, India, Indonesia, Philippines the time spent on smartphones exceeds  that spent on laptops and TV. People are using the handheld devices to search,  connect, read, buy, sell, track, measure, learn and do so much more. Hence, the  question we&rsquo;re asking ourselves is: How do we connect the offline world with  the online to make the engagement and path to purchase more seamless? We look  at it from two different perspectives: </p>
        <p><b>Convergence  of messaging</b><br>
          Most of the  brand clients we&rsquo;ve been working with, have a clear strategy around offline  channels especially Out-of-Home media. In the OOH media, the location plays a  critical role in defining the target audience, viewership and hence pricing.  This made us think: if a user is exposed to the messaging through OOH media in  a certain location, what If we could leverage our proprietary location  technology to re-enforce the same messaging onto his handheld whenever he came  close to the billboard? We ran a few tests across markets. For instance, in  Singapore, a luxury cosmetics brand had a strong messaging on certain strategic  OOH locations. We built a mobile campaign targeting the relevant demography in  those exact locations. So when a user is in the vicinity of any of the  billboards, she&rsquo;s also exposed to the same message on her smartphone. What&rsquo;s  more, upon clicking on the message, there could be multiple actions pre-defined  by the client (call to find out more, follow on social media, download a  coupon, download an app etc). Think of the earlier example I pointed out and  how the experience is changed if the messaging gets re-enforced onto the  device. </p>
        <p>There are  other angles to it as well. The technology enables us to show different  messages and different calls to action depending on which billboard or area  you&rsquo;re close to. Also, we could measure down to the unique users who were  exposed to the ad on their phones in each of the areas. This could, in a sense,  give us an idea of the footfalls across each of the OOH units as well. The  results for such integrated campaigns showed a 130% lift in engagement against a  normal mobile campaign. Think re-enforcement. Think integration. </p>
        <p><b>Offline data  to understand user behaviour</b><br>
          The amazing  upgrade in the capabilities of smartphones in the past few years has posed  challenges and opportunities to the mobile advertising landscape. This has also  manifested itself in the amount of data getting generated by every single user.  At AdNear, our focus has been on using location data generated by these devices  to understand user behaviour and predict intent. </p>
        <p>The  traditional way of predicting behaviour has largely been based on identifying  content consumption patterns (site visits, app usage, video views etc). And  this is where mobile offers a unique opportunity â€“ it &lsquo;s the only media channel  that provides us access to location data at scale. Imagine a location graph of  an individual user as a function of time. I see a mobile device regularly at a  particular location, say X, between 9am and 6pm on weekdays and at say Y,  between 10pm-6am. Now, if we could overlay this information with offline data  from multiple sources (census data, point-of-interest data, survey data  collected by offline channels), this could give me a probabilistic estimate of  the individual&rsquo;s demography, ethnicity, affluence, shopping behaviour and a lot  more. Also, how often the user digresses from base location (Y) and work  location (X) could tell us a lot more about other traits. If the digression  dwell time is at kids&rsquo; play school, we&rsquo;re likely looking at parents, whereas if  this happens to be a different city, we&rsquo;re looking at travel behaviour, which  could be either that of a frequent business traveler (weekday travel, higher  frequency, shorter trips) or a leisure traveler (weekends, lower frequency,  longer trips). </p>
        <p>The  interesting thing here is that brand&rsquo;s own research data could also be baked  into the location graph to derive relevant audience segments. For instance, if an  ice-cream brand&rsquo;s internal research suggests that most of the buyers happen to  be college-going students, this is a data point that could be overlayed to  build custom segment (young demography seen on weekdays in college and  university campuses). </p>
        <p><b>The Big Picture</b><br>
          The larger  point here is that in an increasingly converging media world, offline media  channels and offline data sources can be used more effectively in the digital  marketing spectrum and also, mobile needs to be looked at in alignment with  other media channels and not in isolation. We&rsquo;d love to hear any  thoughts/challenges and happy to share more if you&rsquo;re curious. </p>
											
											
