											<p>Ten years  ago, my father (who is a surgeon by profession) had seven critical things in  his daily life: newspaper, landline phone, pager, medical journals, camera (for  taking images during surgery), his old slide projector and darjeeling tea.  Today, there are two: his iPhone 5 and darjeeling tea.&nbsp;</p>
        <p>The  advent of smarter and more capable handheld devices has changed our lives  faster than we could have imagined. From a marketing perspective, there are  three things that make mobile unique as a medium:&nbsp;<br>
         <ul class="listing">
         <li>Personal: The fact that it's a personal device gives us the opportunity to use  all the data generated from this device to understand its owner.&nbsp;</li>
         <li>Location: Unlike our PCs or television sets, we carry our cellphones almost  everywhere we go and this can give us deep insights into a user's location  behaviour.&nbsp;</li>
          <li>Reach:  There are over 6.5 billion mobile devices on a planet of 7 billion people and  it is expected that sometime in 2014, there will be more devices in use than  people in the world.&nbsp;</li></ul></p>
        <p>A  combination of these three factors makes mobile the most compelling marketing  medium of all, one that cannot be viewed in isolation but in alignment with  other traditional media. The industry is moving at a fast clip and there are a  few key trends I'd like to highlight:&nbsp;</p>
        <p><em>Location  is powerful!</em> <br>
          At AdNear,  we've invested over four years in building expertise to understand a user's  location (with or without on-device GPS activated). This addresses one of the  biggest challenges in location based advertising - scale. Our technology has  given us tremendous success in enabling brands to drive foot traffic to stores  (low barrier to purchase - high lifetime value e.g. McD, Pizza Hut, KFC,  Spencer's etc). We also realised that knowing a user's location is not enough.  Creatives are a critical element in driving engagement. Based on the user's  location, we could dynamically stitch creatives to include &nbsp;relevant  context like address of the closest outlet, distance from store, it's location  on the map etc. And all of this would change depending on the user's whereabouts.&nbsp;</p>
        <p><em>Redefining  LBA - Location Based Audience ™&nbsp;</em> <br>
          While  building this platform, we stumbled upon a larger opportunity. While knowing  &quot;where you are&quot; is useful, knowing &quot;where you have been&quot;  can provide us much deeper insights about a user's behaviour. Imagine that  you're an airline company looking to target frequent travellers. A good way to  do that could be to target people at an airport - the traditional Location  Based Advertising. Perhaps a better way to do that could be to target people  who have been to the airport five times in the past two months and tag them as  frequent travellers - this is what we'd term Location Based Audience ™ (LBA  2.0). Once I know that a user is a frequent traveller, I could target him  anywhere (office, home, on-the-go). You could extend this to students (daytime  at universities/schools), housewives (weekdays in residential areas), urban  dwellers (night time in high end residential areas) and many other behavioural  segments. Attribution of a behaviour to a device (and hence the anonymous user)  requires depth of data (location, ad responsiveness, demography) and it  provides us much larger scale to target them. This is redefining how location  has been used in mobile marketing.&nbsp;</p>
        <p><em>Do more  than clicks!&nbsp;</em>&nbsp;<br>
            While  mobile advertising has grown in the past few years, most of the spends have  been focused on getting clicks on the banner. Even a typical campaign summary  would just have impressions, clicks and cost per click. With growing  transparency in supply, better targeting options and reliable tracking, we'll  see two major shifts:&nbsp;</p>
           <p> <ul class="listing">
          <li>Campaigns with clear performance goals: Not just the initial clicks, but post  click conversion would become an important metric as a means to measure  registrations, calls, coupon downloads etc.&nbsp;</li>
            <li>Reporting and analytics: Instead of just clicks and cpc, campaign reports would  carry a lot more detail like unique users, audience split, location split,  demography etc.&nbsp;</li>
        </ul></p>
        <p><em>Mobile at  the centre of the universe</em> <br>
          While  mobile continues to be a small percentage of digital which in turn is a small  percentage of overall ad spends, we believe that mobile will become  increasingly relevant in integrated campaigns. People are on their mobile while  on the go looking at a billboard, while at work on their desktop, while at home  watching TV and more people are accessing social media through their phones  than through their PCs. Platforms that enable marketers to integrate mobile  effectively with their traditional spends would really help accelerate the  whole mobile marketing eco-system.&nbsp;</p>
        <p>The  mobile ad-tech space is undergoing major shifts which provide immense  opportunities to startups. Use of location as a data point at scale has just  begun and some of the work we've done with brands like Unilever, P&amp;G, Audi,  McD, Samsung etc is a testimony to how powerful this data layer can be. The  next eighteen months are going to be very exciting. Do watch this space for  more updates from our side and here's wishing you a great 2014!&nbsp;</p>
											
											
