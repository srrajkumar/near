											<h2 style="text-align:left;"><?php echo $title; ?></h2>
										<p>The painstaking process of creating actionable location intelligence has borne fruit in the past couple of years. A number of hyperlocal tech companies have refined their technology in tying people to places, and are continuing to find a variety of compelling use cases. While some of the most obvious uses of location data are in understanding the consumer journey and charting attribution, companies like Singapore-based Near are also working on applications in areas like city planning.</p> 

<p>Near, which provides real-time information on places, people and products, enables brands and businesses to “visualize, engage and analyze audience data including their location and behavior for data-driven decisions.” The company is expanding its footprint from its base in Asia and now working its way into markets in Europe and the U.S.  Street Fight recently caught up with Near’s founder and CEO, Anil Mathews, to talk about how location intelligence is progressing in 2017 and what types of new use cases we may soon see.</p>

<h4 style="text-align: left; font-size:18px; margin-bottom:0px;">Tell me a little bit about how Near started and where you fit into the local ecosystem? </h4>

<p>We’re trying to piece together the consumer journey in the real world. … We are big believers that location data is the only data point that connects virtual world to the real world, and that if we stay focused on location data we could actually create a powerful graph of consumer journey across the world. … You know how Google is a search graph and Facebook is a social graph — we want to be the de facto location graph of the world.</p>

<p>There are three data types which power our location graph, and that’s the three Ps: the places, the people, and the product.</p>

<p>The places is our own proprietary data, a lot of it which we have created, collecting actual building shapes. … If you look at specifically the data that’s available in the market for Asia Pacific regions and those parts of the world, it’s been very inaccurate and we have to clean it. So that is a big step that we take, and we have built a lot of IP around it. Today we have around 65 million places that we capture across the regions that we’re present in and then constantly monitor the frequency of people visiting those places and stuff like that.</p>
<p>The second pane of data is the people data. One of the things that you have to understand about how the platforms originated is, so we were looking at if we need to consciously be connected to the device, what are the weights, right? So there are three kinds of approaches we can look at. One is okay, so we could go by telcos and be connected to the device. The second is when there’s no telco connection, there’s wifi connection, so we could look at wifi for others and we connect to it. And the third is obviously apps.</p>
<p>So we invested in three approaches of connecting to the device. … The first is that we have investors who are actual telcos. We have Telstra which is the largest telco in Australia, and we have a firm called Global Brain, KDDI fund, which is the second largest telco in Japan. And we work very closely with Singtel, and Globe in Philippines. So we work with a lot of telcos in order to get the data into our platform.
</p>
<p>The second approach I mentioned is wifi. We work with and we are closing investment with the largest wifi provider in the world. And we get this wifi data in an anonymized way for each of these locations that we are monitoring. So we get massive wifi data.
</p>

<p>And third is connecting to these apps. Connecting to the apps via exchanges or connecting to some of the marquee apps. So that angle is obviously there where we are connected to many apps. Some of them are navigation apps, some are retail-data specific apps and stuff like that. …
</p>

<p>So our USP is bringing in these varied kinds of data, whether it’s telco data, WiFi data or data from apps, onto a single unified platform, which we then use to ally consumers in the real world.
</p>

<p class="img" style=" width: 40%; float: left; margin-right: 20px; padding: 10px;background-color: #FBFBFB;"> <img src="<?php IMAGE('news/anil-mathews.jpg'); ?>" alt="Anil Mathews" style="width:100%;" />
<em style="text-align:left; color:#000; font-size: 11px; line-height: 16px !important; margin-top: 14px; display: block;">Anil Mathews, Founder &amp; CEO, Near</em></p>

<h4 style="text-align: left; font-size:18px; margin-bottom:0px;">Are the brands who use your platform coming to understand the value of location data in a way now that they might not have four or five years ago?
</h4>

<p>Absolutely. Today it’s become a key piece of their marketing plans to use location in various ways. … A lot of these brands are approving their own stores and keeping it to monitor their own log-ins on what kind of volume that comes to their own stores.</p>

<p>When we spoke to a large brand recently — this was a large fast food chain — they said “Hey, the system is allowing us to have a look at the shapes of our buildings because we don’t have that that data in our system. It’s helping us to look at what kind of people are coming into our shops and in our stores and where else they’re going.”</p> 
<p>And that is what actually led us to create the second graph, consumer graph, which is in early stages and closed beta at this moment. It has nothing do with media, which means there’s no advertising angle there, but it’s all about research and looking at what kind of people are coming to your store. Where are they coming from, how much time they’re spending, where else they’re going.
</p>
<p>So we could look at the power of location data, the marketers are really clearly understanding and seeing that. They’re saying: “I used to do all of this where I needed to hire a research firm and they would track to see how many male and female customers are coming through the door, and then run a small sample size, and it could take months, which is costing millions. Now that is available in real-time, on the fly right now in front of me.” That’s the power of location data. So they understand that, and so they’re using it for this consumer insight behavior, they’re using it for attribution, measurement in various ways.
</p>


<h4 style="text-align: left; font-size:18px; margin-bottom:0px;">You mentioned the problem of inaccurate location data. Do you think that’s getting better at all?
</h4>

<p>Globally that’s a big problem. I think it’s one of the biggest problems which location data. Tech companies understand that, but others don’t probably take it that seriously. They think “How bad can it be, right?” But, believe me, we have run tests and figured that in many of these countries less than 20 percent of [location] data is accurate. Which means more than 80 percent of data is actually either noise or inaccurate data that’s been passed and centroid issues and such.
</p>
<p>So I think one of the biggest challenges in creating any platform around location data is cleansing that and making sure the location accuracy is measured continuously and improved upon.
</p>


<h4 style="text-align: left; font-size:18px; margin-bottom:0px;">What do you think is the big opportunity for location data in 2017?
</h4>

<p>It’s one thing about how do we do attribution on digital, which is fairly easy — say you saw an ad on your phone and then you walked into a store. But what about doing attribution with other media, and attribution on TV?
</p>
<p>Just as the location data which actually emerged from this mobile data, you’re able to do attribution on… you could do small geofence around billboards and look at who was seen around that and who walked into a store of that brand. So we could actually cover the entire spectrum of channels, digital and other home media and TV, and run attribution on that from a single platform, just using location data. So that’s the power of location data.
</p>
<p>Second, the consumer insights piece could be as powerful as … what Google Analytics did with websites. And that sort of has been our big vision with location data. Imagine Google Analytics is to come to website on this and say hey, here’s a small API. Put this on your website and I’m going to tell you who came from what, how much time they spend on your website, which pages they went, and stuff like that. We’re thinking we can do that to the real world stores. Without any hardware, without any beacons, without any cameras, without any boxes in your premises, we could tell you just from this location data what kind of people are coming to your store, from where they’re coming, where are their home locations. And then if they came three times to your place, did they go four times to your competition?
</p>
<p>And so all this data interestingly we could show to you in a very visually interesting way or as a data dump which your analysts could use for run reports around that. So the second big use case is consumer insights.  And the third is how we’re working with certain governments on smart city planning. We have been working with certain cities and governments in the Asia Pacific region region, and they want to continuously look at the movement of people from within a certain region so that they can use that data as an input for smart city planning on where the next bus stop should come or where they should lay the next roads and stuff like that. So that’s a really interesting use case. … These are very interesting commercially viable businesses coming out of location data, and they’re becoming more powerful, right?
</p>


 
<h4 style="text-align: left; font-size:18px; margin-bottom:0px;">How do you navigate privacy? Is it different in Asia than it is in the U.S. and Europe?
</h4>
<p>I think the biggest challenges are in Europe, where there are very strict laws on what kind of data can be collected and how can we use and how do we store it. Being in a global platform … it’s always been a challenge because the rules are different in every part of the world.
</p>
<p>I think one of the ways that we have navigated this is by not storing [Personally Identifiable Information], and so we don’t have any mobile phones or emails or names in our data — and that actually helps us stay away from privacy concerns in the governments and of the brands and the marketers that we work with. And that’s also helped us work closely with telcos of the world and the WiFi providers because they’re also paranoid that if you have PII, they will get into regulatory issues. Especially two of our investors, and they want to make sure that we don’t store any of these PII because then if you are mixing and matching our data with their data, then it becomes a bigger concern for them restoring it.
</p>
<p>So no PII. We do obviously follow the usual privacy practices by having the regular audit on privacy compliance. We do have a privacy counsel who’s based out of the U.S. and who constantly helps us in global laws around the privacy and stuff.
</p>
<p>So privacy is a concern and it’s one of the reasons we realize that some of this larger companies are not able to do what we can do because they have too much data — and we saw that work in our favor.
</p>




