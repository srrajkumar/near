											<h2 style="text-align:left;"><?php echo $title; ?></h2>
											
			    <p>Chinese New Year is typically a retail graveyard in countries with a   significant Chinese population as shops close for the holidays.   However,data from Near, a location-based advertising company based in Singapore, may indicate that this is a missed opportunity to make more sales.</p>
                <p>After analyzing location data obtained from over 4,500 app users   through a combination of WiFi and BTS signals emitted from smartphones,   the company found that foot traffic during the past Chinese New Year   weekend on February 1 and 2 was actually higher than usual weekends by   19 percent (see infographic below).</p>
                <p>The study, which looked at Orchard Road, Marina Bay Sands, and   Chinatown, found that Orchard Road remained the most popular destination   for shoppers during the CNY period. However, shoppers typically spend   less time at these places compared to normal weekends, save for   Chinatown. On average, they stayed 2.4 hours in the area during CNY,   compared to 1.9 hours on other days.<br>
                </p>
