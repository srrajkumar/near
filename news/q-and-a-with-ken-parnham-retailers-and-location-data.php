											<h4 style="text-align:left; margin-bottom: 20px;"><?php echo $title; ?></h4>
											
<p class="question" style="font-size:15px;">Ken Parnham, general manager Europe at Near, asks if retailers are making the most of location intelligence in the bid for increased consumer personalisation.</p>

<p>Most retail brands are familiar with the roll call for personalisation. They can list it in their sleep: consumers want relevance, value, and consistency across every channel. But delivering a shopping experience to answer it isn’t so easy.</p>

             							 <p>The traditional path to purchase is gone. Modern consumers are channel-hoppers who might research a product via desktop, compare prices on a smartphone, and finally buy in-store. Indeed, the average consumer uses five connected devices to make a single purchase.</p>
                                
                                         
                                    
                                         <p>But this challenge has created its own solution: data. Note that consumers use multiple connected devices — tech that can access the web anywhere, tech that is mobile. With greater mobile adoption has come a rise in the volume of insights from consumers on the move, opening a world of opportunities for retailers to better connect with them. </p>
                                         
                                       <p>Using location intelligence, forward-thinking retailers can analyse the real-time data generated by a range of devices to build accurate individual profiles, increase loyalty and sales, and even link online behaviour with real-world activity.</p>
                                       
                               
 <p class="question" style="font-size:15px;">So what is location intelligence and how can retailers use it effectively?</p>
 
 <p>Fundamentally, location intelligence is a mixture of business insights and geospatial data, the value of which is revealed by cartographic tools that explore and decode the links between geography, and consumer data. The spatial abilities of this technology have advanced quickly and can now connect business information with data from many areas, such as customer relationship management (CRM) and enterprise resource planning (ERP), and mine the overall pool for useful information.</p>
 
 
 <p> With such sophisticated software at hand, retailers can at last gather all of their data sources in one place and turn the disordered mass into accurate, actionable, real-time insight. Not only does this help retailers understand complex, multi-channel journeys, but it also empowers them to influence consumer decisions by putting data to immediate use, enhancing strategy and performance while keeping competitors at bay.</p>
 
 <p class="question" style="font-size:15px;"> What can it do for retailers?</p>
 <p>The greatest retail benefit of location intelligence is its capacity to identify key moments for delivering meaningful interactions, online and offline, in real-time. Whether it’s a consumer who has been researching tablet prices and is currently standing outside a rival tech store, or a shopper checking offers for specific stores online, location intelligence can pinpoint opportunities to strengthen relationships, loyalty, and sales.</p>

<p> For example, Starbucks has done just that by adding a mobile order option to its app. Instead of waiting in the queue, consumers can find nearby branches and place orders before they arrive — making their experience more convenient, enjoyable, and repeatable.</p>

<p>Yet location intelligence has more to offer than timely messaging. It can also be used to inform business decisions, such as where to place new stores, and facilitate precise attribution by linking device identifiers to location footprints and measuring every interaction, and purchase, in a consumer’s journey. Retailers can also use their understanding of consumers to action omni-channel targeting and closely track its impact across channels.  </p>
<h4 style="text-align:left; font-size:17px; margin-top: 30px;">Top 3 retail applications of location intelligence:</h4>		
<p><span class="question"> 1. Merchandising mix: </span>The appeal of a product can depend greatly on shopper demographics, time of year or even the weather, so retailers must be agile. With real-time location intelligence, they can instantly identify who is buying what, where, and when — ensuring merchandising is always aligned with current needs and trends.</p>		

<p><span class="question"> 2. Experiment with pricing and promotions: </span>By extracting the hidden value within their own data, retailers can experiment with offers, discounts, and stock rotation to determine which strategies are most successful and discontinue those that are not, without waiting for external analysis.</p>	
	
<p><span class="question">  3. Omni-channel campaigns: </span>At present, many retailers work with different data providers, making it difficult to generate a complete view of individual consumers and engage them across channels. By amalgamating insights, location intelligence enables them to understand where messages will have the greatest impact and deliver the best experience.</p>
<p> With 40% of global shoppers frequently purchasing via mobile and almost 50% keen to see tailored recommendations in store, it’s clear that consumers expect a connected shopping experience. And location intelligence offers a way to deliver it.</p>	
<p> By utilising the latest technologies to blend mobile data with business insight, retailers can follow the twists and turns of channel-hopping consumer journeys, identify the best time to reach them, and finally answer the roll call for personalised, data-driven shopping.</p>					 


