											<h2 style="text-align:left;"><?php echo $title; ?></h2>
											
             							 <p><em>We are well past the tipping point of traffic from mobile devices overtaking that of desktop. But are brands embracing this shift and are media agencies properly equipped to aid them in planning effective mobile campaigns? ExchangeWire spoke to leaders across the industry to understand how mobile adoption is progressing.</em></p>
										 
<h4 style="text-align:left; font-size:18px;">The juggernaut of media consumption</h4>											 

<p>The UK is now a smartphone society. Ken Parnham, general manager Europe, Near commented: “There is no doubt that mobile is transforming consumers’ lives – and the marketing landscape as a result. Last year, mobile became the <a href="http://consumers.ofcom.org.uk/news/uk-now-a-smartphone-society/" target="_blank">number one</a> device for users to access the internet, with mobile revenues for UK publishers increasing by 80%.”</p>

<p>Michael Craig, director of agency relations, Byyd added: “As consumers become more confident in purchasing high-ticket items on their smartphones ‘mobile’ edges ever closer to conversion metrics.”</p>

<h4 style="text-align:left; font-size:18px;">Brands still struggling to understand mobile</h4>

<p>The consensus across the industry is that we aren’t there yet. Mobile is a vast space with opportunities galore, but brands and agencies alike struggle to cut through the noise, understand who their audiences are and what a successful mobile strategy actually looks like. There is a general lack of understanding of the mobile ecosystem.</p>

<p>Celine Saturnino, head of media planning, Total Media said: “From a brand perspective, it’s a mixed bag in terms of comfort around mobile, which is driven by how mobile-first the audience is, KPIs of the campaign, and measurability. Most brands acknowledge the importance of mobile as a channel; but challenges around measurement and attribution have prevented investment scaling relative to audience growth.”</p>

<p>Ken Parnham, general manager Europe, Near added: “Brands in certain verticals, such as retail and FMCG, are embracing mobile more than other sectors. Luxury brands were once hesitant about adopting digital channels, such as mobile, due to misconceptions about their complexity and fears that instant access could devalue products. Key players such as Gucci, Dior, and Louis Vuitton are leading the way and banishing these misconceptions.”</p>

<p>David Simutis, product marketing manager, Sizmek commented: “Rich media is a proven tactic in aiding brand recall, and the stats speak for themselves – just touching an ad increases engagement by 20%, whereas interactive video is found to boost awareness 51% over non-interactive ads, thus doubling purchase intent. But too few advertisers are captivating consumers through these validated techniques.”</p>

<h4 style="text-align:left; font-size:18px;">Agencies are attempting to drive change</h4>
<p>Brands need to be educated on the value of mobile. Agencies need to ensure that they are fully equipped to deliver this. Mobile seems to be the hot potato; it exists within an agency, but doesn’t follow the rules of standard digital planning and media buying and is struggling with an identity crisis. However, it does seem that the increasing prevalence of mobile is causing agencies to quickly adapt to accommodate it.</p>
<p>Celine Saturnino, head of media planning, Total Media, explains where mobile fits within their agency: “As a behavioural planning agency, for us the role of mobile sits with the strategy and planning teams who understand how audiences interact with mobile and its role in different categories. This insight is then used to shape the mobile activation plan, how it integrates with other media and the channels best used to deliver that. This could be any combination of mobile search, paid social, digital display on mobile devices, cross-platform activity with publishers and mobile-first programmatic partners. Responsibility for the delivery of this will then sit across various activation teams. As an agency, we continue to invest in mobile specialist resources to allow us to stay ahead of an ever-changing landscape. However, as mobile is part of every media channel now, it makes sense for each activation team to understand and activate mobile, rather than it being a separate division.”</p>
<p>Scott Curtis, European mobile strategy & development director, Starcom Mediavest Group, told ExchangeWire that they approach mobile as a behaviour, not just a channel. “Mobile permeates the agency across all disciplines – everyone incorporates mobile into their jobs: from strategy & planning, to activation, and to traditional channels too.</p>
<p>“Furthermore, because of the multitude of applications, the adoption of mobile throughout the agency has resulted in a more varied approach to simply executing pure-media campaigns. Instead, we build technology, produce content, devise creative and partner with startups and tech innovators.”</p>
<p>Agencies know what they have to do keep the momentum going and it does require patience and a solid understanding of a brand’s infrastructure and internal politics to get things over the line.</p>
<p>Michael Craig, director of agency relations, Byyd said: “Increasingly, agency digital planners set out with the intention of planning mobile KPIs as awareness, consideration and purchase intent. However, as the campaign draws to a close, the client will often ask for conversion rates. It’s a similar issue desktop faced with Google and last click.”</p>
<p>“Fundamentally, it’s our job as an agency to help clients understand the importance of mobile because it is unavoidable for them”, said Scott Curtis, European mobile strategy & development director, Starcom Mediavest Group.</p>

<h4 style="text-align:left; font-size:18px;">Still a long way to go</h4>
<p>Neither brands nor agencies can claim to have their mobile offering nailed; but it is important that they understand where the weaknesses lie and embark on a mutually beneficial journey together.
“Brand attitudes are moving in the right direction, but using data to drive mobile marketing still requires a sell in”, said Ken Parnham, general manager Europe, Near. “Although agencies have better mobile understanding and technical ability than they did two years ago, their ability to utilise mobile data effectively needs further development. While the industry is aware of the opportunities available, mobile still only accounts for <a href="https://www.exchangewire.com/blog/2015/10/08/65-of-publishers-see-ad-blocking-as-a-threat-81-of-consumers-research-products-before-purchase/" target="_blank">a quarter</a> of total digital ad spend and adoption across different vertical sectors is varied.”</p>
<p>Michael Craig, director of agency relations, Byyd pointed out that, “mobile is increasingly being separated [on media plans], but still, in the main, it’s included in desktop budgets”.</p>
<p>Greg Carroll, UK country manager, StickyADS.tv added: “The mass migration to consuming media on mobile devices is continuing, and clients and their agencies need to be on top of mobile and the various formats available.”</p>
<p>Ken Parnham, country manager Europe, Near said: “As more consumers embrace the technology, marketers are racing to catch up. At present, I would say that the industry is at the midlevel adoption stage; mobile marketing activity is growing, and the number of campaigns using audience and location data to improve targeting is increasing.”</p>
<p>Parnham added that data for mobile measurement is still viewed as an optional add-on. “Brand and agency adoption of data as a facilitator for mobile marketing success is still very basic and will continue to be until its full capabilities are realised.”</p>
<p>David Simutis, product marketing manager, Sizmek agreed that, despite the fact that mobile will soon command the clear majority of display advertising, the industry is still failing to plan campaigns using mobile’s full range of capabilities.</p>

<h4 style="text-align:left; font-size:18px;">The future is bright</h4>

<p>Brands are starting to wake up. Mobile strategy is developing, ad formats are adapting, and the industry is working on ensuring that consumers are viewing ads and content with which they want to engage.</p>
<p>Michael Craig, director of agency relations; Byyd said: “Developments in mobile planning over the last 18 months have not only integrated browsing behaviour but, uniquely, location data, which can be used as a proxy for audience. This has enabled growth in mobile across multiple verticals, in particular Retail and FMCG.”</p>
<p>Craig added that the inclusion of trials and studies in the mobile planning process is helping this education and understanding for brands. “Increasingly, agencies include brand effectiveness & footfall studies in the planning process as this can complement the agreed KPIs of CTR (in its most basic sense), expansion rates or dwell time.”</p>
<p>Greg Carroll, UK country manager, StickyADS.tv commented on how the industry is adapting to the fact that a mobile strategy can’t be a ‘one size fits all’ approach: “Standard display advertising has always struggled to translate to the small screen and that has given rise to new ‘native’ formats that better engage audiences through the use of content. The next evolution will be how to feasibly scale these formats through the use of programmatic.”</p>


