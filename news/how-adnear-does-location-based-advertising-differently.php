											<h2 style="text-align:left;"><?php echo $title; ?></h2>
											<p>Founded in 2008 by serial entrepreneur Anil Mathews, Near is a location-based advertising network that’s approaching the game differently. While most location-based ad networks use GPS or operator data, Anil wanted to make Near different. His approach is to rely on mobile towers and Wi-Fi signals.</p>
<p><strong>Mathews said:</strong><br>
"Near has invested in mapping cell tower signals and wifi signals in every population centre in South Asia by literally driving around and collecting footprints."</p>


        <p>Today, the company has operations and datasets in Singapore, India, Australia, and other Southeast Asian countries. Collecting all that info might sound like insanely hard work but the rewards have proved to be great. By collecting signal footprints, Near can target users across all platforms without relying on GPS capabilities and with a high level of granularity. He added:</p>
        
        <p>"We use this location data to understand a user’s history – where he is spending most of his time and what he’s doing at different times of the day and different days of the week. This helps us understand whether he’s a traveller or a student etc. This level of data analysis enables us to provide the most evolved form of targeting to marketers."</p>
        <p>And indeed, marketers are impressed. Near reaches over 50 million unique users every month across India, Southeast Asia and Australia and is working with some of the largest brands in the world. Near’s innovative approach has also earned it $6.5 million in investments from Canaan Partners and Sequoia Capital late last year.</p>
        <p>It hasn’t at all been easy collecting data. Near took three long years to build. But Mathews felt that time was needed to ensure that Near was building a future-ready solution and not another location-based ad network that relied on GPS and telco data.</p>
        <p>"It was very important for us as a company to ensure our offering was future-ready so that when we launch it is still the most relevant form of reaching the audience," he said.</p>
