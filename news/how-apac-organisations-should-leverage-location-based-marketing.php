											<h4 style="text-align:left;"><?php echo $title; ?></h4>
										<p>As mobile devices have become an integral part of our lives, it presents an opportunity for marketers to tap on to location-based marketing. But are organisations in the Asia Pacific region (APAC) ready to do so, and how should they go about doing it? We find out from Smriti Kataria, Director of Research for Singapore-based company Near.  </p> 



<h4 style="text-align: left; font-size:18px; margin-bottom:0px;">Why should organisations/marketers adopt location-based marketing tools? </h4>

<p>Internet of Things (IoT) has been a game changer; changing the way we live, work and entertain ourselves. It is impacting all types of businesses, above all their ability to connect and control their environments. With all the different types of data that IoT provides, which is valuable to an organisation to drive consumer engagement and new revenue streams, it is a lot to keep up with.</p>

<p>Therefore, well-built and well planned location-based marketing tools converge multiple data sets that emerge from IoT. By connecting digital users to the real world, marketers are able to drive real-time consumer engagement throughout the consumer journey across both physical and digital touch-points. Given this level of connectivity, it is finally possible for marketers to accurately map, track and re-evaluate how they engage each type of consumer across their respective journeys, all in real-time. Real-time is key here because in the past, marketing strategies have all been based on past consumer behaviour which often brings in the problem of relevance and can often be a 'hit' and 'miss' strategy. With continuous access to new and changing data on the spot, marketers can change their engagement strategy to ensure they stay relevant and top of mind with the ability to move beyond the promotion stage.</p>

<p>However, the benefits of location data extend beyond marketing. It is a powerful planning tool where granular competitor information can be extracted to guide decisions around a firm's operations and business strategy.</p>


<h4 style="text-align: left; font-size:18px; margin-bottom:0px;">What are some of the challenges faced by marketers when they adopt location-based marketing tools?</h4>

<p>Converging data from IoT into a location-based marketing tool is one aspect of it. But what happens to all the emerging data from CRM, spend and social? Having them operate separately will create a confusing marketing strategy. Therefore a powerful location-based tool is one that uses and streamlines all these types of data streams to then connects with the consumer in real-time. Marketers should also optimise their brand strategies to understand the audience and location that fits best to maximise engagement.</p>



<h4 style="text-align: left; font-size:18px; margin-bottom:0px;">How is your company helping organisations address the problems mentioned above and improve their marketing strategy?</h4>

<p>Near's flagship product <a href="https://near.co/products/allspark.php" target="_blank">Allspark</a> allows brands and enterprises to leverage third-party and proprietary data sets in real-time. The current data sets consolidate consumer location, behaviour, interest and demographics. With certain tweaks, the platform can also fuse data from CRM, IoT, spend, and social streams making it an extremely powerful tool and shifts the control to brands. Brands can experiment, optimise and get the best return on their spends. </p>


<h4 style="text-align: left; font-size:18px; margin-bottom:0px;">How receptive are consumers in APAC towards location-based marketing? Can you share how you've helped organisations in the region improve their business?</h4>

<p>Location-based marketing is becoming popular amongst APAC marketers as they start to realise the value of this tool. Increasingly, we are getting queries about Allspark so we are spending a lot of time doing demonstrations to showcase the power of our tool. The feedback has been fantastic which is further helped to create new user cases as we go along.</p>



<p>Near's platform has be utilised across many businesses in the region. We ran a campaign with Shell in the Philippines, where we used location data to promote products and services to consumers in both its own and competitor stores. Location data not only helped Shell to gather rich insights about consumers and target them at the most relevant moment, but it also helped them to measure impact. Shell saw a <a href="https://near.co/media/pdf/latest-research/shell-competitor-targeting-consumer-insights.pdf" target="_blank">14</a> percent increase in foot traffic, with 20 percent of this jump coming from the competitor outlets. </p>





