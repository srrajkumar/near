										<p>Male tourists were seen 1.5 times higher compared to female ones.</p> 

<p>During the recently celebrated Chinese New Year, location intelligence platform Near found out that male tourists were seen 1.5 times higher compared to female tourists. Around 61% of the 48,000 tourists and 36,500 who flocked seven locations in Singapore were male according to the study conducted by Near.</p>


