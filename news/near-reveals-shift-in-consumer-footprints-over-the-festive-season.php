											<h4 style="text-align:left;"><?php echo $title; ?></h4>
											
             							 <p>Near, a Singapore-headquartered location intelligence platform, has today released a report on the change in behaviour and location footprints of Singaporeans from Christmas time to Chinese New Year. </p>
                                         <p>The research looks into the different patterns of consumer traffic around the festive season according to their location, and identifies how peak traffic and mobile engagement varied among users analysed during these two events.</p>
										 
<h4 style="text-align:left; font-size:18px;">Key findings from the research:</h4>											 
 <p><ul class="listing">
			 <li>The rush hours across all locations over both Christmas and Chinese New Year periods were mostly during working hours, suggesting that consumers were perhaps shopping during their lunch break or even taking time off work to stock up for the festive season&nbsp;</li>
			 <li>During Christmas, most users were more interested in music-related apps. Over Chinese New Year, this changed to photography, perhaps indicative of the desire to capture the colours and picturesque Chinese New Year decorations across Singapore&nbsp;</li>
			 <li>48% of the same people who were around Bugis Junction during the Christmas period were seen to be spending most of their time in Chinatown during Chinese New Year&nbsp;</li>
			 <li>Males and females exhibited differences in their preference for shopping locations over both holiday seasons&nbsp;</li>
	  </ul></p>
      <p style="text-align:center;"><a href="<?php PDF('consumer-intelligence/shift-in-consumer-footprints-from-christmas-to-chinese-new-year.pdf'); ?>" target="_blank"><img src="<?php IMAGE('news/consumer-footprints-from-chirstmas-to-cny-in-singapore.png'); ?>" style="width: 100%;"  alt="CONSUMER FOOTPRINTS FROM CHRISTMAS TO CNY IN SINGAPORE" /></a></p>
<p>What we can deduce from this report is that customer behaviour is ever-changing, it’s not prescriptive and it shouldn’t be considered as all as one. We have witnessed significant changes in behaviour and movement regarding the people of Singapore over this time period, an example of how real-time data can prove to be very useful to those that have this location intelligence at their fingertips. </p>

<p>For example, the LTA can use this information to determine the impact of certain holiday seasons on road congestion. Retail stores can assess the likelihood of increased foot traffic in a particular area to determine if more staff needs to be rostered on. Brands can analyse if an outdoor ad campaign received high visibility in particular locations by measuring footfall in the area. </p>



<p>Previously considered a powerful tool at the hands of brands in order to assess and measure attribution for campaigns, it is now becoming a critical asset for enterprises of any nature. In terms of business strategy, mobile data is a fountain of consumer insight and should be deeply analysed by organisations in order to beat the competition, improve service and therefore, inevitably increase a company’s bottom line.  </p>

