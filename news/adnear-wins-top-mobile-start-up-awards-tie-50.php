											<p>
                Big data company Near, the largest Location Based Audience player in Asia Pacific, is excited to be named one of the Top 10 startups in the mobile category at the prestigious 2014 TiE50 Awards Program in San Francisco.</p>

<p>Thousands of technology start-ups worldwide competed in TiE50, Silicon Valley's premier annual awards program that recognises the 50 hottest tech start-ups globally. Winners were announced on Saturday, May 16, at TiEcon, the world’s largest conference for entrepreneurs.</p>


<p>&ldquo;We are honoured to receive this prestigious award, which is a true testament to the impact our technology has on brands globally,&rdquo;says Anil Mathews, Founder and CEO of Near. &ldquo;Near is about connecting brands with consumers intelligently through mobile, an increasingly important marketing platform.&rdquo;</p>


<p>Major brands like Audi, Coca Cola, Unilever, P&G, Google, Pizza Hut, Adidas, Vodafone, Samsung and many others are already using Near's audience and location based mobile advertising solution to reach their customers and draw valuable insights about them in various geographies.</p>


<p>Returning for the sixth year in a row, this year's TiE50 was open to all technology companies worldwide. To determine the winner, a panel of experts screened the companies and created a list of the &ldquo;2014 TiE50 Top Startups.&rdquo; This list was then reviewed by a screening committee of deep domain experts. About 100 of the best of breed companies were selected to the elite TiE50 Finalist pool. Finally, this pool of TiE50 Finalists were judged by a panel of highly accomplished serial entrepreneurs, venture capitalists, angel investors, CEOs and well-rounded technology experts, to determine finalists and winners. Companies were evaluated on three parameters: Business Model, IP Value and Leadership Team.</p>


<p>&ldquo;In the six short years since inception, the TiE 50 program has become a global brand that attracts thousands of companies worldwide for this recognition,&rdquo; said Venktesh Shukla, president of TiE, Silicon Valley. It is unique in terms of rigor of selection and the broad scope of companies that it reviews for this award and is one of TiE Silicon Valley's most successful programs.</p>


<p>At TiEcon 2014, 20 of the top startups from each of the five segments – Software, Mobile, Internet of Things, Cleantech, Life Sciences – were chosen to pitch their ideas, and winners were announced on stage that evening.</p>


<p>&ldquo;This year the TiE50 Awards Program screened more than 2800 companies from 27 countries. The finalist pool is represented by early through late stage companies. A star attraction of the conference is TiE50 Finalist presentations. Audiences are treated to great ideas and amazing technologies&rdquo; said Ram K. Reddy, the program chair.</p>


<p>The TiE50 track record since its inception in May 2009 shows that 94% of the winners and finalists have been funded, attracting Billions in investments. Many of these companies have been acquired, merged or gone public.</p>

<p>For further details on the TiE50, visit  <a target="_blank" href="http://www.tie50.org/">www.tie50.org</a>.			    </p>
