<p class="question" style="font-size:15px;">What is your favourite personal gadget? </p>
<p>To be honest, none of my gadgets beat a traditional pen and writing pad. I carry one in my backpack everywhere and it’s saved many a day.</p>

<p class="question" style="font-size:15px;">Do you have a favourite piece of personal software? </p>
<p>GNU Emacs, the text editor is one of the most versatile pieces of software. I really appreciate how a good editor can improve productivity.</p>

<p class="question" style="font-size:15px;">What was the first piece of technology you got really excited about? </p>
<p>My first contact with LISP/Scheme – the programming language in grad school. It provided a new way to look at the world of computing. And reading SICP - the book by Sussmann really opened up vistas.</p>

<p class="question" style="font-size:15px;">What device improves your life most at work? </p>
<p>My laptop keeps my personal and professional life in sync. Other form factors have just not worked as well. Emails/Coding/Doc writing – you never knows when an idea or inspiration will strike.</p>

<p class="question" style="font-size:15px;">What software keeps you most productive at work? </p>
<p>I can’t be without Google Email and Google Docs to help me coordinate and communicate with my peers and teams.</p>

<p class="question" style="font-size:15px;">Is there any technology that has become extinct that you would like to resurrect?</p>
<p>Couple of them actually - Punch cards were good for taking notes. Not a tech per se but it would be great to bring back more face-to-face communication rather than via apps/messaging etc. Though these have given voices to many folks, there’s still nothing like developing the skill for real human engagement.</p>

<p class="question" style="font-size:15px;">Is there any technology you would personally recommend that you don’t think enough people know about? (Hardware or software, work or personal) </p>
<p>Learn LISP/Scheme/Clojure – especially for aspiring developers. It really helps develop the mental models to be a better programmer. All the basics are showing up in different current programming languages.</p>

<p class="question" style="font-size:15px;">Do you think anything about your personal use of tech would surprise your colleagues? </p>
<p>I still love writing code. As Alan Perlis famously said, “You think you know when you can learn, are more sure when you can write, even more when you can teach, but certain when you can program.”</p>


