											<h4 style="text-align:left;"><?php echo $title; ?></h4>
										<p>For Anil Mathews, founder and CEO of Near, a location intelligence platform, guesswork is the enemy of effective marketing. Near aims to provide real-time data intelligence through a platform that enables companies to collate, visualize, and interpret insights about people, products, and places in order to inform business decisions.</p> 

<p>“Assumption is no substitute for accuracy; if brands want campaigns to succeed, they need to know exactly what is working and what isn’t, and they need to know quickly,” he tells RTBlog. So where does real-time attribution data fall into this equation? RTBlog had a chat with Mathews on these issues.</p>


<p style="color: #db1a21;font-size: 14px; text-shadow: 0px 0px 0px #db1a21;"><span class="question" style="color: #db1a21;font-size: 14px;">RTBlog:</span> Marketers are looking for precise measurement and to attribute each marketing dollar spent to the right channel. What are the leading challenges in this area, and what are the solutions? </p>

<p><span class="question">Anil Mathews: </span>In today’s complex marketing landscape, a precise view of the impact every cent generates is crucial to efficiently manage media spends. However, building a cohesive picture of the responses and conversions that campaigns generate across multiple channels and devices isn’t easy.</p>

<p>As emerging technologies such as augmented reality and smart assistants continue to change the way consumers interact with brands — not to mention extending the array of screens they use to do so — it’s getting even harder to achieve successful multichannel, cross-screen attribution.</p>
<p>Many brands are still using models such as first- and last-click attribution, yet such methods ignore the nature of modern consumer journeys. Individuals now switch between a diverse range of online and offline channels, so their final purchase decision is likely to be influenced by numerous touchpoints. It’s important to understand the value of every single one.</p>
<p>While we’ve seen progress in campaign tracking during the last year — with better solutions being offered to quantify each touchpoint on the path to purchase — most brands are only just beginning to scratch the surface of holistic performance assessment. We’re moving in the right direction, but there is still a long way to go.
</p>
<p style="color: #db1a21;font-size: 14px; text-shadow: 0px 0px 0px #db1a21;"><span class="question" style="color: #db1a21;font-size: 14px;">RTBlog:</span> What does truly effective cross-device attribution look like? What do you hear marketers want in this respect?</p>

<p><span class="question">Anil Mathews: </span>To be effective, cross-device attribution must be exact and available in real time. Budgets are stretching across a constantly expanding selection of channels, and audience attention is, in turn, becoming more difficult to capture. To maximize relevance and conversions, it’s increasingly important for brands to instantly gain accurate intelligence about campaign success, so focus and spend can be shifted accordingly.</p>

<p>For example, a fashion brand campaign might be split between physical stores, outdoor media, social media and display, with the balance tipped toward display because it’s worked well before. But attribution may show social media is actually driving more consumers into stores.</p>



<p>If this insight is delivered in real time, the brand can immediately divert spend where it will produce the greatest return and thereby increase its in-store sales. This is what some in the industry are calling the “walk-to rate,” or measuring the specific touchpoint that inspired an offline sale, rather than just putting it down to the most recently clicked ad.</p>

<p style="color: #db1a21;font-size: 14px; text-shadow: 0px 0px 0px #db1a21;"><span class="question" style="color: #db1a21;font-size: 14px;">RTBlog:</span> What type of real-time data is needed to help "solve" the cross-device/cross-platform attribution challenge? Where does it come from, and what kind of tech stack is needed to simplify this process? </p> 
<p>And that is what actually led us to create the second graph, consumer graph, which is in early stages and closed beta at this moment. It has nothing do with media, which means there’s no advertising angle there, but it’s all about research and looking at what kind of people are coming to your store. Where are they coming from, how much time they’re spending, where else they’re going.
</p>
<p><span class="question">Anil Mathews: </span>Real-time attribution data is the Holy Grail the industry has been looking for. It’s an unrivaled source of knowledge that enables brands to create the best media mix, streamline spend, optimize existing and future strategy, and gain a competitive advantage. It’s everything advertisers need because the data itself is a blend of all cross-channel campaign elements: TV, mobile, the Web, in-store activity, digital out-of-home screens — you name it, real-time attribution tracks, assesses, and makes sense of it.</p>



<p>If we’re looking for an example of particularly useful data sets, I would say mobile offers an essential bridge between digital and the real world. By harnessing the location footprints mobile activity generates, brands can identify individuals and move with them as they move from channel to channel, to the eventual purchase.</p>
<p>The technology used to process this information must be built not only to aggregate and sort multiple data streams at once, but also continually monitor them to avoid duplication and errors. Such platforms might use methods like cartographic data, whereby geospatial data is used to verify data, or multiple source linkage — comparing streams to enhance accuracy.</p>


<p>The most important thing to remember is that while speed is crucial, so is precision. Real-time attribution can be the ultimate advertising solution, but only if the data is correct.</p>




