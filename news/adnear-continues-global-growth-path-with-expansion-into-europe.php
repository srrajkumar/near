											<h4 style="text-align:left;"><?php echo $title; ?></h4>
											 <p><em>Ken Parnham, appointed General Manager of Europe, based in London office</em></p>

<p>Near, the leading mobile data intelligence platform, today announced its expansion into Europe with Ken Parnham – formerly Managing Director of data privacy management company, TrustE – appointed as General Manager of Europe, to be based in its new London office.</p>

<p>Parnham, a data and technology expert with almost 20 years digital experience, joins Near from TrustE where, as Managing Director, he advised brands on data and privacy issues. Prior to TrustE, Parnham held leadership roles at OpenX – where he successfully launched the company’s real-time bidding market place in EMEA - Wanadoo (Orange-France Telecom), and 24/7 Media. </p>

<p><img src="<?php IMAGE('news/ken.jpg'); ?>" style="height:auto; float: left; margin-right: 15px;" alt="Ken Pranham"/></p>

<p>Near’s platform combines historical location and contextual mobile user data with proprietary technology, creating consumer insights at scale that help advertisers achieve their real-time mobile marketing and offline attribution goals. To date, the company has profiled more than 700 million device users. In the two years since its foundation, Near has rapidly built out from its Singapore headquarters into Australia, South East Asia and Japan. Global marquee brand clients include P&amp;G, Audi, Unilever, BMW, Adidas, Ford, Samsung, and IKEA.</p>

<p>Shobhit Shukla, Chief Revenue Office at Near commented: &ldquo;By combining mobile user data with our proprietary technology, Near provides marketers with the opportunity to connect the digital user to the real world.Having established our proposition in APAC, expansion into Europe is a logical next step in our strategy, which is being driven directly by advertiser demand in the region.&rdquo;</p>

<p>Parnham said of joining Near: "I'm delighted to be charged with bringing AdNears's unique offering to European advertisers. Near empowers advertisers to take control of how they define their custom audiences, and leverages the scale of mobile to uncover real-time consumer insights and then apply them to offline attribution. "</p>

<p>Parnham’s appointment is the latest in a series of recent additions to AdNear’s leadership team, with Cameron Curtis joining as General Manager for Australia and New Zealand. </p>

<h4>About Near</h4>

<p>	<a href="http://www.near.co" target="_blank">Near</a> is the largest location intelligence platform providing real-time information on places, people and products. The Near platform powers its flagship product to leverage historical location and context for data driven marketing. </p>

<p>Founded in 2012, Near is headquartered in Singapore with a presence across Australia, South East Asia, Japan, India and Europe. To date, the company has more than 700 million profiled audiences and have put it to work for  marquee brands such as P&G, Coca Cola, Ikea, Audi, McDonald’s, Toyota, Nike and Samsung. </p>
<p>Near is backed by investments from leading venture capitalists Sequoia Capital, JPM Private Equity Group, Telstra Ventures and Global Brain Japan.</p>
