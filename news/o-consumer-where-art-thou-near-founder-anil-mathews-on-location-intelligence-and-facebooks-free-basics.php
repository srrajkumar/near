<h4 style="text-align:left; font-size:18px; margin-top: 10px;">It was once said that you can tell a lot about a man by his shoes. With today’s levels of smartphone penetration, you can probably tell more by his location data.</h4>

             							 <p>By tracking the consumer’s journey both online and in the real world, location intelligence platforms are able to identify travellers, shoppers, students and other segmented groups, allowing marketeers to target more precisely.</p>
                                         <p>Using 8.4bn consumer location footprints from people’s mobile phones, Near has developed profiles for more than 700m consumers from 5,000 cities, making it the largest platform in the world. Its product, Allspark, allows clients to visualise and analyse this data in real-time.</p>
                                         <p style="margin-bottom: 40px;">Chief executive and founder Anil Mathews tells City A.M. how it can be used to plan and execute campaigns, measure physical store visits and understand consumer behaviour, both in and out of store. </p>
										 
<h4 style="text-align:left; font-size:18px;">How will location intelligence insights be used by marketers in the coming years?</h4>											 
 <p>As technology becomes more sophisticated, location data will become vital to making critical business decisions. Brands are already starting to use location intelligence to identify individual users and map their journey across screens and channels. In the future, this data will be used to inform in-store merchandising and track return on investment by measuring visitation rates – it might even determine where stores will be.</p>
      
<p style="margin-bottom: 40px;">Location data is also set to transform outdoor advertising, informing the selection of best performing sites, understanding consumers around areas, enabling more relevant targeting and effective promotional tie-ups with stores based on real-time consumer preferences. The insight it offers into consumer habits and preferences may even make it a valuable resource for product development and consumer discovery.</p>

<h4 style="text-align:left; font-size:18px;">What are the main difficulties in collecting and harnessing location intelligence?</h4>

<p>The main challenges include gathering the location footprints in real-time, identifying the same user across multiple data sources, and ensuring that data is both accurate and actionable in real-time.</p>
<p style="margin-bottom: 40px;">Various techniques can be used to overcome these difficulties. At Near, we use cartographic data, triangulation and multiple source linkages, as well as our own first-party data to deliver maximum accuracy. We also monitor incoming data at each stage of the pipeline to ensure that any inconsistencies can be immediately detected and corrected. While this allows us to keep data reliable and secure, greater standardisation is needed at an industry level to accommodate the growing number of data providers and ever-changing digital environment.</p>

<h4 style="text-align:left; font-size:18px;">What is the future of location intelligence outside of marketing?</h4>

<p>The applications of location intelligence within the marketing sector are immense, but it has the potential to do so much more. The insight it offers on individual movements, trends and habits means that location intelligence will soon become an essential data point in a variety of sectors. For example, it can be used to increase government efficiency with effective urban planning and land transport decisions. In terms of healthcare, it can assist in multiple areas from the planning of new hospitals to sending state alerts on epidemics. It can help to reduce credit card fraud by mapping the transaction location to the user location, and adding additional security measures.</p>

<p style="margin-bottom: 40px;">On a more domestic scale, it will become an integral element of the modern “smart home” as the Internet of Things makes the way we live more technology-focused. With location data, users will be able to turn their mobile devices into a universal control for lighting and heating systems, whether they are at home or not. It could also be used in public buildings to maximise efficiency – detecting whether rooms are occupied and adjusting heat or light accordingly.</p>

<h4 style="text-align:left; font-size:18px;">With India overtaking China as the world’s fastest growing large economy, and with smartphone penetration there expected to reach 314m by 2017, how does your Indian data operation compare with other markets? And why should marketers pay particular attention to India?</h4>

<p>Data gathering in India is tough because of limitations in network and internet connectivity in rural areas. That said, India has been one of the first countries we gathered data for.</p>
<p style="margin-bottom: 40px;">It’s still too early for data to be valued in the region as much as in western countries, but it’s emerging faster than we all think. The business opportunity in the next two to three years could be significant, if positioned well.</p>

<h4 style="text-align:left; font-size:18px;">As a data-driven marketer operating in India, where do you stand on Facebook’s Free Basics scheme to bring access to selected internet services to less developed countries, and the decision of the Telecom Regulatory Authority of India (TRAI) to ban it?</h4>

<p>The Free Basics offering in India was intended for a lot of users to get their first internet experience using limited free websites. Whether the opinion of these citizens would have been biased due to limitations in information available on these free websites remains debatable.</p>
<p>As an offering, Free Basics was definitely not net-neutral. By definition, it ups certain options over others, hence the “non neutral” decision by TRAI. However, Free Basics had the potential to bring limited free internet to the country’s millions of citizens, and could have worked better if it was made available as unlimited paid internet after a defined period of time.</p>


