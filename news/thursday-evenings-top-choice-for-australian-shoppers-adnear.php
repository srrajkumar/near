											<h2 style="text-align:left;"><?php echo $title; ?></h2>
											<p>Australian shoppers prefer visiting grocery stores and general retail outlets on Thursday evenings, according to big data company Near's latest research on Australian shopper behaviour. Whilst grocery shopping seemed to be the great equalizer, with nearly the same number of men as women frequenting supermarkets, ladies were more likely to hit the retail stores. </p> 

<p>Near's research was conducted between February and April this year among 300,000 mobile users in five cities - Sydney, Melbourne, Perth, Adelaide and Brisbane. The study tracked shopping habits of homemakers, professionals, the affluent and students based on their location data to form behaviour insights.</p>

<p>Of the total sample size, Melbourne had most grocery shoppers at 43 per cent, followed closely by Sydney at 41 per cent, whilst most general retail shoppers were located in Sydney (52 per cent), followed by Melbourne (34 per cent). Shoppers in Adelaide, Brisbane and Perth made up less than 20 per cent each of the respective grocery and general retail shopping demographic, underscoring the markets' relative size to the two bigger cities of Sydney and Melbourne.</p>

<p>Homemakers shop the most - they were the largest audience segment in general retail outlets, the only exception was in Sydney with professionals coming out on top. There were also more homemakers, followed by professionals, who shopped for groceries. </p>

<p>Grocery shopper numbers were generally steady across each week of the month, with a slight dip in the last week, likely attributed to dwindling household budgets. General retail shopper numbers in Melbourne, Sydney and Perth were consistent throughout the month, but Adelaide and Brisbane witnessed dips in the last two weeks of the month. </p>

<p>Although Thursday was most popular for shopping, many general retail shoppers in Melbourne and Sydney were also at stores on Sunday. Weekends, however, were less popular than weekdays for grocery shoppers across all five cities.</p>

<p>While the grocery shopping gender split was near parity, with men at 46 per cent and women at 54 per cent, majority of general retail shoppers were women (70 per cent).  Adelaide also witnessed more men than women grocery shopping, the only city of the five.</p> 

