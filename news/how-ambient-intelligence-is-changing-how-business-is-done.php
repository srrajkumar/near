			    <p style="color: #db1a21;font-size: 14px; text-shadow: 0px 0px 0px #db1a21;">What is the future of data? </p>

<p>The mass creation of data, especially on the post-PC devices, is increasingly due to the proliferation of such devices in developing countries. This is in addition to people who have used smartphones for years.</p>

<p>The cost of storing data has dropped and as a result, more data is being created and then stored. It used to be that companies spent time thinking about storing data. Nowadays, the focus is on how to organise, contextualise and monetise data for different uses to understand the consumer base.</p>

<p style="color: #db1a21;font-size: 14px; text-shadow: 0px 0px 0px #db1a21;">What is the importance of ambient data, a service that Near provides?</p>

<p>Ambient data is generated from smart environments that helps to map the physical word. Until recently there was not a way for businesses to take advantage of this data. Whereas in the digital world, many e-commerce sites track their customer’s digital footprint and then send emails regarding the interested item/s when it is on a promotion, Near seeks to change this by allowing organisations to quantify and measure the physical world.</p>

<p>Near provides the data so that companies can analyse and better understand their customers’ habits and mind-sets. Such information helps marketers reach out to the right audience, or be where their consumers are.</p>

<p style="color: #db1a21;font-size: 14px; text-shadow: 0px 0px 0px #db1a21;">Could you share any examples of data-driven decision that are made using Allspark, SaaS audience product by Near?</p> 
<p>To give you a broad overview of how different industries are using <a href="https://near.co/products/allspark.php" target="_blank">Allspark</a>, here are two examples.</p>
<p>The first is from the banking sector. A bank was trying to sell home loans to customers likely to buy a house. The bank licensed Allspark from Near and identified housing project locations. They then used Allspark to monitor activity during inspection hours and viewing hours to understand behaviour and demography of the customers. Once sufficient data has been gathered--such as the average pricing of the houses and the range of disposable income of the customers--the bank can then use this dataset to tweak offers for home loans to be more enticing to the group of potential homeowners depending on their budget.</p>
<p>Traditionally, sample surveys have been conducted at specific areas to gather data similar to this, but it is dependent on the market researchers and who they speak to. However, this is time-consuming and expensive to conduct especially over a large number of locations.  The wonderful thing about Allspark is that it is easily scalable – the bank was monitoring close to over 120,000 locations. Such an ability allows the bank to have greater flexibility and confidence in tailoring its loans as compared to sample surveys.</p>
<p>The other example would be an Out of Home (OOH) company. It was difficult to measure the effectiveness of the billboards. Placing a billboard in a specific location is expensive, hence the need to justify such cost. With Allspark, we can compare the number of consumers (traffic flow) around each billboard and see if the billboard had an impact on actual spending habits. For example, if a consumer sees a billboard for a fast food chain, our clients can see whether the consumer then went to one of the fast food outlets.</p>


<p style="color: #db1a21;font-size: 14px; text-shadow: 0px 0px 0px #db1a21;">From a personal and managerial perspective, how do you manage such fast growth of the business?</p>
<p> In certain circumstances, constraints are good as it helps to focus the decisions. For Near, an ‘artificial’ constraint that we put on ourselves is to be as lean as possible in terms of manpower. One benefit is that it allows Near to be selective about the type of talent that we want to hire. By hiring high-performing individuals, this allows Near to give more freedom and responsibility (a core company belief) to colleagues. This is especially important as Near operations are across six time zones.</p>
<p>Most multinational corporations (MNCs) have centralised decision-making processes.  Near needs to have decentralised decision-making. Think of a spider web. We purposely push decision-making to the outer nodes of the web, whereas in many start-ups, decision-making sits in the centre with the founders. This gives the employees the freedom to experiment, and allows them to learn from their mistakes if they fail. While there is no right or wrong way to lead the company, this is how Near has decided to grow.</p>


<p style="color: #db1a21;font-size: 14px; text-shadow: 0px 0px 0px #db1a21;">What advice can you share for people who aspire to found a start-up?</p>

<p>There needs to be a good product market fit--which is sometimes overlooked--but very important. The timing is critical for product success. If the product does not fit market needs/timing, it will not sell.</p>
<p>Following that, hiring the right people is important. The initial core group is extremely critical. It is the old adage: “A players hire A players, B players hire C players.” Your core group determines the efficiency and effectiveness of your company, which directly affects the chance of success. Especially in a start-up, hiring can be compared to marriage. If the start-up goes through a rough patch, then a good fit with regard to talent is essential to getting the company back on track.</p>
<p>Though controversial, I would say that entrepreneurs should also have a bit of irrationality. This will help to ride out the rough time when others say that running a successful start-up is an impossible task. It reminds me of a Steve Jobs quote:  “I'm convinced that about half of what separates the successful entrepreneurs from the non-successful ones is pure perseverance.... Unless you have a lot of passion about this, you are not going to survive. You are going to give it up. So you've got to have an idea, or a problem or a wrong that you want to right that you're passionate about; otherwise, you're not going to have the perseverance to stick it through.”</p>


