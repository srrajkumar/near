											<h4 style="text-align:left; margin-bottom: 20px;"><?php echo $title; ?></h4>
											
<p>The Singapore-based company's Allspark platform gives marketers a glimpse into where customers are spending their time — which allows for improved targeting both on and offline.</p>

<p>In the early days of location-based marketing, Marketers are beginning to understand the value of understanding where customers are spending their time outside of a store’s vicinity — both so that they can reach them there, and so they can understand what this real-world behavior says about purchase intent.</p>

             							 <p>This is the goal that Singapore-based company Near hopes to address with <a href="<?php echo SITE_URL; ?>allspark" target="_blank">Allspark</a>, its mobile-first audience cloud platform that aggregates geo-data from over 700 million devices globally.</p>
                                         
                                    
                                         <p>The data is organized by people and the places they are visiting and represented on a user interface in real time. This lets marketers curate audience segments on the fly, measure their marketing spend’s real-time impact by looking at offline attribution, and plan both mobile and offline marketing campaigns based on where consumers spend their time.</p>
                                         <p>“If you’re a telecom company and you see most of your customers are hanging out at a particular coffee chain or a café, you might want to partner with them for a promotion,” said Shobhit Shukla, CRO at Near. “We started seeing a lot of that happening. It’s not just for mobile ad spend planning; it’s also for a lot of offline marketing planning.”</p>
                                         
              <h4 style="font-size:18px; text-align:left;">GeoMarketing: Near was founded in Singapore in 2012. Now, you have offices across Sydney, Tokyo, London, and Palo Alto. How did the company get its start, and how has it seen the location landscape evolve since then? </h4>                           
                                         <p><span style="color:#ec222b;">Shobhit Shukla:</span> We started from Asia and then started moving west, like a lot of companies that start in the U.S. and go east. At that point in time, the ecosystem was just emerging.</p>
										 
						 
 
 <p>Then we partnered with a lot of apps, and we started getting a lot of data. We had built this interesting technology that enabled us to get location, especially in emerging markets where a lot of people either weren’t sharing location or a lot of the data was not accurate.</p>
 <p>Actually, [location data accuracy] is still an issue here, even in the most developed markets. Being able to get location where it’s not readily available plus also being able to clean it, analyze it, get rid of a lot of noise — we found that that was powerful. Those were some of the capabilities that we built. Eventually, we realized that one of the most interesting use cases of that technology was in marketing and advertising.</p>
 <p>We started collecting location data in pretty large volumes, using that to understand consumer behavior.</p>
 <p>It’s like this: Typically, in the digital ecosystems, if you add something to your shopping cart and if you decide not to buy it, the [retailer] knows it and then they target you with suggestions. It’s been extremely hard to do that in the physical world. You could walk into a café and look at the menu, not like anything, and then walk away. It’s very hard for them to know what happened.</p>
 
 <h4 style="font-size:18px; text-align:left;">So how does Near address this challenge — the idea that it’s more difficult to understand customer journeys and intent in the physical world — with the Allspark platform?</h4>

<p>While every action in the digital world is quantifiable, in the physical world it’s extremely hard. But now, with mobile devices, what you can get from consumers [is their] real-world location.</p>
<p>You can get the physical movements of people and then map them to understand their behavior.</p>
<p>For example, a lot of brands and partners that we were working with started asking us to help with not just understanding and improving their ad spends, but also understanding what’s happening in their retail store, how many people are walking in, how much time they are spending in the retail store, where are they coming from, where are they going.  All of those insights matter.</p>

<h4 style="font-size:18px; text-align:left;">What brands with a brick-and-mortar presence have you worked with to do this?</h4>

<p>Ikea is one of our large partners. We’ve done a lot work with McDonald’s. We’ve worked with Pizza Hut.</p>
<p>The top three or four verticals that we work with are obviously retail and fast food, because the whole idea lends itself pretty well to that vertical. Auto dealers are also always looking at really fresh data around people who just recently walked into a car showroom for a test drive, because they’re the ones who are likely going to go and buy a car. Those three or four verticals we’ve seen and done a lot of work.</p>
<p>What we realized was that a lot of our partners were asking for two or three things quite often. First: They wanted the flexibility to curate their own segments. A retail client would come to us with a set of store locations and say, “Can you actually monitor and see people who visited these stores and where else are they going?”</p>
<p>Obviously, it helps in planning the mobile ad spend, but it’s not just that. There are a lot of benefits to understanding consumer behavior better. I’ll give you an example. One of the clients that we work with in Japan, they use a lot of this data to plan their offline marketing strategy — they want to know where their consumers are spending most of their time when they’re not in their stores.</p>

<p>This helps them to do cross-promotions and branding partnerships with those merchants, for example. If you’re a telecom operator and you see most of your customers are hanging out at a particular coffee chain or a café, you want to partner with them for a promotion.</p>
<p>We started seeing a lot of that happening. It’s not just for mobile ad spend planning, it’s also for a lot of offline marketing planning. The second thing that they started asking us for was “Can you use this data for targeting on any kind of platform?” Generally, a lot of our partners want to work with Google.</p>
<p>We did a partnership with Google where we push out all the data. Using Allspark, you can create a segment and you can start pushing out the data into Google DoubleClick and you can target people through Google’s platform.</p>
<p>Finally, the most interesting bit was using the platform for measurement and attribution — people who got exposed to an ad versus people who didn’t see an ad, how many of them walked in? What is the likelihood of a person seeing an ad and walking into a store to buy something? With location data, you can do all of these things.</p>

<h4 style="font-size:18px; text-align:left;">How do you aggregate that location data? One of the major challenges in the industry is dealing with customers who don’t have location services turned on, handling geo-data inaccuracies within cities, et cetera. How will you deal with that moving forward? </h4>

<p>Our process is that we’ve gone and taken a slightly different horizontal approach, where we look at multiple sources rather than a single source, because that gives us a little more confidence when we are getting location data. We look at data from apps; we look at data not just when it’s available through GPS but also when it’s available from a cell tower and wi-fi signals.</p>
<p>We’ve done a partnership with a bunch of wi-fi infrastructure companies so that even when the user doesn’t open an app and walks into a wi-fi zone, we know that they’re there.</p>
<p>The hard part of using this to understand behavior is knowing the context. I know that you’re here, but that doesn’t mean that you’re in a café or the hotel above it? Knowing exactly what the building is, is extremely important. There’s a lot of error, there’s a lot of noise in that as well.</p>
<p>We are constantly going through the process of cleaning up a lot of that data to understand exact context, and that has [gotten more accurate] the more we’ve done it.</p>






