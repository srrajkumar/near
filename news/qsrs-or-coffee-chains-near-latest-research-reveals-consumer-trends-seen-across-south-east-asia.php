											<p class="img">
             							 <p>Jakarta, Singapore and Hong Kong had higher preference for Coffee Chains while Manila, Bangkok and Kuala Lumpur favoured QSRs, according to the latest research released today on <b>‘Coffee Chains and Quick Service Restaurants’</b> by Near, a Singapore-headquartered location intelligence company. The research also indicated that, overall, QSRs had higher repeat customers compared to Coffee Chains across all cities<sup>1</sup>  covered under the study.</p>
											 

<p>One of the key findings in Singapore was that, the consumers saw Coffee Bean and Pizza Hut as the most preferred brands in their respective categories. On the contrary, Pacific Coffee had higher repeat customers with over 35% of the customers having visited more than once compared to other Coffee Chains. As for QSRs, McDonald’s saw higher repeat customers with over 34% of customers having walked into the store more than once.</p>

<p>Furthermore, the study revealed gender differences in brand preference, regardless of QSRs or Coffee Chains in Singapore. Near found that males mostly preferred Coffee Bean whereas females preferred Starbucks. Similarly, men frequented Pizza Hut whereas women chose McDonald’s.</p>

<p>With regard to trends seen by audience segments in Singapore, Affluent and Professionals had highest preference for Coffee Bean while Homemakers and Students preferred Starbucks. Affluent and Homemakers preferred Pizza Hut while Professionals and Travelers preferred McDonald’s. Frequent patrons of QSRs tended to do so on Tuesday evenings between 5pm and 9pm while Coffee Chains had the most footfall on Wednesday between 4pm and 7pm.</p>

<p class="img"><a href="<?php PDF('consumer-intelligence/consumer-insights-for-fast-food-or-fast-coffee-across-sea-2015.pdf'); ?>" target="_blank"><img src="<?php IMAGE('news/coffee-chains-and-qsr-brands-insight.png') ?>" style="width:100%; height:auto" alt="QSRs or Coffee Chains" /></a></p>


<p>“With more and more fast-food restaurants and coffee chains popping up across Asia, it is vital for marketers to be well-versed with the defining trends among their consumers,” said Smriti Kataria, Director for Research and Marketing, Near. “The use of our proprietary technology has allowed us to glean significant insight into the profiles and demographics of their frequent patrons. Armed with this knowledge, marketers can now tweak their offerings and strategies to maximise their marketing effectiveness,” added Smriti.</p>

<p><b>Regionally, the study revealed several interesting statistics too</b> – Manila had higher repeat customers with over 41% of them having visited Coffee Chains more than once while Hong Kong had higher repeat customers of over 44% of them having seen in QSRs more than once. </p>

<p>Among the three age groups analysed, people over 36 were seen to be the least regular patrons of both QSRs and Coffee Chains across all six cities that were studied, further exhibiting how preferences differ greatly among people from different age groups.</p>

<p>The study also found that in general, Students were in favour of QSRs to Coffee Chains, except in Jakarta, where the reverse was found to be true. Affluent across SEA except in Hong Kong and Jakarta had high preference for Coffee Chains to QSRs. </p>

<p>Both males and females in Jakarta showed highest uptake for Coffee Chains whereas the highest uptake for QSRs was seen by both in Bangkok. Most of the consumers were seen spending their time on Social, Entertainment and Music apps on their mobile devices. </p>

<p>Coffee Chains had high footfall during late afternoons and early evenings while QSRs saw heavy footfall during late evenings in all cities. Interestingly, Coffee Chains saw high footfall on Tuesdays while QSRs had high foot traffic on Saturdays.</p>

<p>The report provides relevant consumer insights for Coffee Chains and QSRs during the month of May, 2015, in the major South East Asian regional markets of Singapore, Hong Kong, Jakarta, Bangkok, Kuala Lumpur and Manila. The report analysed 225,000 users and QSR brands covered by the study include: Jollibee, Pizza Hut, Subway, McDonald's and KFC. Coffee brands covered by the study include: Starbucks, Pacific Coffee, Excelso, Cafe Amazon, The Coffee Bean & Tea Leaf, Seattle's Best Coffee, Old Town White Coffee and Simplylife.</p>


