<em style="text-align:left; color:#000; font-size: 11px; line-height: 16px !important; margin-top: 14px; display: block;">Anil Mathews, Founder &amp; CEO</em></p>

<p class="question" style="font-size:15px; margin-top:20px;">What examples are you seeing of financial services using location intelligence to make more informed business decisions?</p>
<p><span class="question">Anil Mathews:</span> Location intelligence has so many applications as a decision-making tool that the industry is only just beginning to discover. Right now, its finance-specific uses fall into two categories: single and periodical. In the single-use camp we are seeing location intelligence informing new investments within areas such as mining, agriculture, retail, and commerce. When it comes to periodic use, it tends to be called upon for larger scale investments in areas like transportation services, public utilities or health, inventory management, and logistics.</p>
<p>Both areas, however, utilize the same core insight, namely:general spatial viability, consumer dynamics in relation to prosperity of people in particular regions — and the products or services they regularly buy — and other geographic consideration sen compassing local culture, crime rates, or local attractions. This information can form an accurate basis for decisions great and small.  Whether it’s everyday tasks like establishing credit risk, small business potential for loans and mortgage suitability, or deciding whether to invest in public companies, which new ventures to support and how to manage private equity, location intelligence is indispensible.</p>

<p class="question" style="font-size:15px;">How can location intelligence help combat financial fraud and crime? </p>
<p><span class="question">Anil Mathews:</span> Fighting financial fraud and crime in the real world is an age-old battle and now the rise of digital has extended the front-line to the virtual space. To stay on top of these issues tools that tackle both are essential and this makes location intelligence a key line of defense.</p>
<p>It can be used to set transaction limits that keep consumer and organizational spend safe. If a purchase is made from a location that does not match previous purchase and investment patterns — or a pre-defined area that can range from a country, state or even household — the transaction will be marked as potentially fraudulent and can be stopped.</p>
<p>Similarly, fraud in areas such as income, property, and sales tax can be identified using location insight gained from credit card usage. Where consumers live, the places they visit, and how often they travel can all help to build a picture of standard behavior. Any unexplained irregularities that indicate additional income can be investigated to uncover possible fraud.</p>


<p class="question" style="font-size:15px;">How can location intelligence help financial services reach Millennials?</p>
<p><span class="question">Anil Mathews:</span> Millennials live a high proportion of their lives via smart, web-enabled devices that go with them everywhere and produce endless streams of data about what they do, where they do it, and how they prefer to communicate. And for financial services providers hoping to engage millennial attention, extracting the right insights from this rising tide of location-specific data is vital.</p>
<p>According to the results of a global study conducted by Telstra in collaboration with Near, 58% of millennials prefer personalized financial advice and 49% feel it should be delivered via a mobile app. With accurate location intelligence, financial service providers can build individual consumer profiles that enable them to quickly and accurately adapt their offering, and communications, and keep up the fast pace of millennial expectation.</p>


<p class="question" style="font-size:15px;">What is the future of location intelligence in the finance sector? </p>
<p><span class="question">Anil Mathews:</span> Location intelligence is becoming an intrinsic element of increasing efficiency and effectiveness within the finance sector, and in the future it is set to be more so.</p>
<p>At a high level, it will soon have a greater influence over multi-sector ventures, tracking and optimizing ROI on investments, and economic planning activities — such as economic zones and free trade corridors — as countries try to reinvent political and administrative borders. On a smaller scale, location intelligence is due to continue on its current course in areas like risk assessment and expand into new frontiers, like retail and facilities analytics.</p>
<p>Within the next few years, I believe it will be instrumental in making e-commerce frictionless, as well as streamlining urban planning, logistics, and overall optimization of investments in private and public infrastructure. It sounds bold, but the wheels are already in motion.</p>



<p class="question" style="font-size:15px;">What advice would you give financial services looking to better utilize their customer data?</p>
<p><span class="question">Anil Mathews:</span>  Unify data and make it actionable. At the moment, the sector is struggling with the ‘water, water everywhere’ scenario in that there is plenty of information available about consumers — financial status data in one place, transactions and consumption data in another — but it is stored in silos and therefore of little use.</p>


<p>There is an urgent need for financial services providers to embrace tools that amalgamate their fragmented pools of information and turn them into cohesive, actionable insight. Only by understanding the full context of each consumer or enterprise decision will they be able to enhance service quality and outshine the competition.</p>


