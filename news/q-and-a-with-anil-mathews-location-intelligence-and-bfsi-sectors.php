<p class="question" style="font-size:15px;">What is location intelligence and what benefits does it offer the finance sector?</p>
<p><span class="question">Anil Mathews:</span> Essentially, location intelligence is the missing ingredient of context. It blends geographical perspective, the ‘where’, with the other key contextual data points — the ‘what, who, when, and why’ — to create a single, complete picture of individual or enterprise activity.</p>
<p>The result is real-time, actionable insight that not only provides a better understanding of the factors involved in a particular action, such as a consumer deciding to buy a car, but also when and where that consumer might make future purchases — and how they can be influenced.</p>

<p>For the financial sector, such information is invaluable in every area from retail to real estate. Using location intelligence businesses can identify the links between geographical factors — like spatial attributes, the weather, urbanization, and environmental constraints — and activity at an individual, and organizational level. With a bird’s eye view of economic context, businesses can ensure their strategy is one step ahead of emerging trends, market shifts, and competitors. </p>
