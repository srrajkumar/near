											<h4 style="text-align:left; margin-bottom: 20px;"><?php echo $title; ?></h4>
											
<p>Near, the leading location intelligence platform, today announced its integration with Oracle Data Cloud to make Near’s global audience data available through Oracle’s BlueKai Marketplace. </p>


<p>The integration will enable users of Oracle’s BlueKai Marketplace to access Near’s audiences, which are curated using real-time location and behavioural data. By overlaying Near’s data on their existing targeting data, marketers will be better able to reach their target audiences, understand and improve their campaigns, and optimise media spends. Since the Near platform builds audiences from device data, the agreement also dramatically expands the global reach of Oracle’s Bluekai Marketplace with new audience sets.</p>
<p>The Near platform combines and analyses data from over a billion devices, allowing marketers to gather insights on consumers and places, act on them in real-time, and measure impact. Near is also an existing Gold level member of the Oracle PartnerNetwork (OPN). The Oracle Data Cloud works with key data players and integrates with more than 200 media companies, including publisher exchanges, agency trading desks and DMPs who can use the data for reaching consumers more effectively.</p>



<p>“Location is a crucial data set for most brands today. Through our integration with Oracle’s BlueKai Marketplace, brands will now have unique access to audience sets built from location data that will help them target these audiences and benefit from an advanced understanding of their consumers,” said Aditi Kohli, Senior Director for Data Partnerships, Near.</p>



<p>“We are pleased to integrate Near’s global location data into Oracle’s BlueKai Marketplace, so marketers can take advantage of robust location-based intelligence for smarter decision making around the world,” said Rich Harris, Group Vice President, Oracle Data Cloud.</p>



<h4 style="font-size:18px; text-align: left; margin-bottom: 0px;">About Near:</h4>
<p>Near is the largest location intelligence platform providing real-time information on places, people and products. The Near platform powers Allspark, its flagship product enabling customers to visualize, engage and analyze audience data including their location and behaviour for data-driven decisions.</p>
<p>Founded in 2012, Near is headquartered in Singapore with offices in San Francisco, London, Bangalore, Singapore, Tokyo and Sydney. To date, the company has data from over a billion devices and has put it to work for marquee brands including P&amp;G, Coca Cola, Ikea, Audi, McDonald’s, Toyota, Nike and Samsung.</p>
<p>Near is backed by investments from leading venture capitalists Sequoia Capital, JP Morgan Private Equity Group, Telstra Ventures and Global Brain Japan. Visit <a href="https://near.co" target="_blank">www.near.co</a> to find out more.</p>

<h4 style="font-size: 18px; text-align: left; margin-bottom: 0px;">About Oracle Data Cloud:</h4>
<p>Oracle Data Cloud operates the BlueKai Marketplace, the world’s largest audience data marketplace. Oracle Data Cloud is the leading global Data as a Service (DaaS) solution, offering access to more than $3 trillion in consumer transaction data, two billion global consumer profiles, and 1,500+ data partners. Oracle Data Cloud integrates that data with more than 200 major media companies, including publisher exchanges, ad networks, DSPs, DMPs, and agency trading desks. For more information and free data consultation, contact The Data Hotline at www.oracle.com/thedatahotline.</p>


