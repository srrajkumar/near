<p class="question" style="font-size:15px;">Location data is a powerful tool, when used correctly, but there are concerns within the industry as to the quality of that data and concerns from consumers regarding privacy. How can location data be used effectively and where is it being done well? ExchangeWire speak with Ken Parnham (pictured below), general manager, Europe, Near to find out. </p>

<p>Near have recently launched their Allspark product, a mobile-first audience cloud powered by the Near platform. It’s a tool that allows brands to visually create audiences based on location, overlaid with demographic data. According to Parnham, <a href="<?php echo SITE_URL; ?>allspark" target="_blank">Allspark</a> is moving away from insights and attribution and looking at where the user is in the real world – what they’re doing, how long they will stay there, and where else they’re going. It’s also not about who someone is, but where they’ve been. “There is an inertia”, said Parnham, “we need to move away from the standard demographics of age and gender”.</p>

             							 <p>According to Parnham, brands are using location data effectively, but they could do a lot more too: “It depends on the campaign objectives. Many brands are buying through Facebook, due to the fact that its data is deterministic, but I would argue that inferred data could be more powerful.” Building a user profile, based on where that user is and has been, can help to understand where that user is going and can be much more valuable than just knowing the user’s age and last purchase.</p>
                                
                                         
                                    
                                         <p>When discussing how advertisers fund their location campaigns, Parnham said that there aren’t new budgets for location campaigns, it has to come from somewhere else and he confirmed that much of the budget is coming from social media: “It’s a channel that is seeing saturation, as it’s limited to audience-based targeting, with some location, but the limit is being reached.”</p>
                                         
                                       <p>When asked why location is increasing in popularity for advertisers, Parnham said that it’s very much down to the freshness of the data: “The whole point of location and audience profiling is building it up for 30 days and keeping it fresh – we are real-world beings moving to multiple locations.” According to Parnham, Allspark connects to app data and pings to the server. It builds a profile, putting a user in an age bucket and looking at their affluence, whether they’re a business traveller, etc. It also looks at app consumption, locations the user is seen at and cross-references this with third-party data. “There are billions of pings per day”, said Parnham, “and the key is to keep the data fresh.”</p>
										 
					 
 
 <p>However, he was keen to point out there are problems that exist with location data: “People in the industry don’t trust location data, as location providers aren’t being transparent. A lot of the data comes from location exchanges – lat and long pings, which aren’t necessarily accurate. It’s not malicious, it just isn’t accurate.” When asked whether fraud affects location data, Parnham responded that there isn’t much of a risk from the bots and fraud side of things, but the cleanliness of the data from location exchanges is an issue.</p>
 <p class="img" style=" width: 40%; float: left; margin-right: 20px; padding: 10px;background-color: #FBFBFB;"> <img src="<?php IMAGE('news/ken.jpg'); ?>" alt="Ken Parnham" style="width:100%;" />
<em style="text-align:left; color:#000; font-size: 11px; line-height: 16px !important; margin-top: 14px; display: block;">Ken Parnham, General Manager, Europe, Near</em></p>
 
 
 <p>Location targeting holds myriad opportunities, but at what point does it cross a line for consumers? “If brands can use it sensibly”, argued Parnham, “the more enhanced it becomes and the better it is.” He said that, from a consumer-engagement perspective, it’s about finding out what people want and bringing the brands closer to the consumer using personalisation; but that personalisation doesn’t need to make a user feel uneasy. “The cookie-targeting scenario got out of hand”, said Parnham, “and it wasn’t used in the right way for behavioural targeting. With location you can be more targeted, but it’s not like you have an arrow in your back – it needs to be engaging and interesting, not creepy.”</p>
 
 
 <p> How does location targeting look from a market-to-market perspective? According to Parnham, the Japanese consumer is very mobile-led and that market was a huge early adopter. The US have also adopted it, but Europe is slightly more cautious about it. The UK, specifically, want to know about the source of the data, whether it’s clean and hygienic. “The UK are more thoughtful in their campaign planning”, mused Parnham, “but there are some markets out there that are like the Wild West.”</p>
 <p>Given that different markets are at different levels of sophistication, when it comes to effective location targeting, where will location fit in the future of advertising? The location possibilities are endless, according to Parnham. “Location is becoming part of a necessary media channel buy now”, he confirmed. “What’s next with location is omnichannel – tracking a person or identifying a person in the real world and cross-referencing that on the bigger screen.” Parnham envisions fewer adverts per screen or per channel, but more direct messaging.</p>

<p>When discussing the developments in OOH automation, and how location could fit into that, Parnham heeded that, while there are many opportunities with location and OOH, it can’t be taken too far: “Purely personalised ads could result in data infringement, in the EU at least.” So, privacy is an issue, but so is scale: “OOH is a one-to-many medium and it would be challenging to flip that into purely a one-to-one channel.”</p>

<p>When asked if location could be viewed as the underdog channel, Parnham didn’t agree or disagree, but did say that, despite spend going up and things going in the right direction, the people flying the flag for location don’t have the biggest voices: “At this stage, it could be a situation of people not wanting to shout too loudly and make the mistakes that have been made generally in mobile advertising in the past. Google and Facebook also aren’t really helping to push the location agenda.”</p>


