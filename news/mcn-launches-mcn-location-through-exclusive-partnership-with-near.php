<p><em>Offering provides advertisers with mobile location targeting capabilities in real world locations</em></p>

<p>Multi Channel Network (MCN) today launched MCN Location, a leading mobile location intelligence platform enabled by the exclusive Australian representation agreement with Near.</p>

             							 <p>Near are locational data specialists providing privacy compliant real-time data on people and places. They entered into an exclusive sales representation agreement with MCN in May this year, allowing the sales representation company to sell location-based mobile advertising packages to agencies. </p>
                                
                                         
                                    
                                         <p>MCN Location allows advertisers to create and target mobile audience segments based on location. Near’s insights and technology platform locates mobile devices and verifies those locations. This location data is overlaid with third party data sources like Roy Morgan Helix personas and other behavioural data sets allowing MCN to create highly targeted, bespoke audience segments for brands.</p>
                                         
                                       <p>Additionally, MCN Location, using the Near platform, has mapped over one million points of interest including stadiums, shopping centres, retail outlets, restaurants and many others. We can also add advertisers’ own locations like retail outlets or dealerships. The result is a rich, multi-layered data source that gives advertisers the ability to target their audiences on mobile at scale.</p>
										 
					 
 
 <p>MCN digital partnerships and product development director, Suzie Cardwell, said MCN Location uses Near’s rich insights to give advertisers highly targeted mobile advertising delivery, based on location and context. </p>
 
 
 
 <p>“We’ll be working with Near to extend the location datasets across the rest of our digital network to offer location based targeting across our premium assets,” she said. “Over the longer term, we’ll integrate Near’s location data across our market leading data product, Multiview, to connect with audiences across any screen, at any location, and at any time.”</p>
 
 
 <p>As part of the launch MCN has hired Rob Marshall as Mobile Sales Manager in Melbourne. Marshall is the first dedicated mobile sales representative appointed in MCN Melbourne.</p>
 <p>He has been working in the media industry for over six years, with the last three years in mobile. Marshall has joined MCN to help the continued growth of our mobile offering and spearhead the MCN and Near partnership in the Melbourne market.</p>

<p><span class="question">Marshall said:</span> “I believe that the MCN business model of multi-screen integration is a proven success and bringing a location data play to the table is yet another huge step forward for the business.”</p>

<p>Near offers clients unparalleled engagement with mobile audiences through a unique proprietary technology that uses WiFi networks, 3G and 4G tower reception, and GPS location data. The system allows the collection of some of the most accurate mobile location data in the industry.</p>


