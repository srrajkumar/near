             							 <p>The ubiquity of smartphones today means that mobile data has a role to play when it comes to creating new business strategies, according to location intelligence firm Near in a new report.</p>
											 

<p>Brands that successfully tap into the vast amounts of useful data in terms of our habits, interests, interactions and intents can hence gain the requisite consumer insights to beat the competition or improve their service – as well as their bottom line.</p>

<p>“Brands can use mobile data to gain unique business insight into potential and existing customers. This insight can be used to power strategy, marketing and operational decisions,” said Near in its report. “These could vary from choosing a new store location to measuring results of marketing spends.”</p>
<h4 style="text-align:left; font-size:18px;">Mobile data for more effective targeting of customers</h4>

<p>More specifically, knowing where its potential customers are and what they like allows brands to offer more nuanced marketing that can be used to more effectively target them.</p>
<p>The result is that brands are able to move beyond the current “haphazard” state of pushing static promotions out the door, be it via the use of non-reactive one-way in-store displays or untargeted online banner ads. Importantly, insights gained from mobile data can be used as the basis for business strategy.</p>
<p>Near did some digging through its data to illustrate how mobile data can offer important strategic insights. For example, Near says that Android smartphones (56%) is the prominent mobile share in Singapore over iOS smartphones (40%). And males appear to favor Android – more configurable perhaps? – while females use iOS devices more frequently.</p>
<p>Near in October last year also did some analyzing and took a closer look at five prominent shopping destinations in Singapore: Westgate Jurong, Marina Bay, Ion Orchard, Bugis Junction and Raffles City. The conclusion? While Singaporeans did not appear to have a fixed day set aside for shopping, peak timings start from around 5pm until about 8pm.</p>
<p>Ultimately, mobile data could open a valuable insight into the “what”, “where”, “when” and “how” of what people buy.</p>
<p>“For brands, this means a significant opportunity to bridge the gap between consumer and business strategy by emphasizing targeted consumer engagement,” concluded Near in its report. “Bringing together the physical and digital world will ultimately add to consumer measure and marketing success, offer broader business decisions for brands, as well as boost sales.”</p>
<p>Near gets its data from smartphone apps that integrate its code, which numbers in the hundreds of thousands of apps today. Data is transmitted in an anonymized format with no personally identifying information.</p>

