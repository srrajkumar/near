											<h4 style="text-align:left;"><?php echo $title; ?></h4>
											 <p>We are thrilled to share that we won the Gold in the <a href="http://www.marketing-interactive.com/mob-ex-awards/sea/winners" target="_blank">Best Campaign for Tablets</a> category at the Mob-Ex awards held yesterday in Singapore. Near & Mediacom won this award for the innovative and interactive Audi A8 campaign.</p>

<p>Mob-Ex Awards, over the years have established themselves as the prime awards that appreciate and celebrate excellent work by brands and agencies in the mobile marketing industry. According to Mob-Ex, 29 categories were open for contest, in which 145 entries made the final cut from a record number of more than 340 received this year.</p>

<p><b>Benjamin Pavanetto from Near (second from the left) along with our Agency partner Mediacom, and the Client Audi (holding the award)</b></p>

<p>The objective of the "The Art of Progress" campaign was to drive product awareness of Audi A8, particularly focusing on Matrix LED feature and also other features like Head up display, Interior, B&O Sound system, etc. in the Singapore market. The challenge was to reach its target audiences and enable them to get an almost real life experience of the car, without their presence in the Audi showroom. The campaign needed to be interactive to get insights on the various features showcased.</p>

<p>Customised audiences for Audi (Audi Audience), were created by identifying affluent & auto enthusiasts in targeted locations in Singapore. The audiences comprising of affluent & auto enthusiasts were built by putting in context the historical location data and analyzing the content consumption behaviour of the users over a period of time. In-app ads were shown to the "Audi Audience" who were seen using tablets & smartphones.</p>

<p>A special gyroscope creative was created for Audi, which opened to a 360-degree view of car interiors. Moving the tablet enabled the user to see the complete car. Users could explore various features like MMI Navigation plus, MMI Touch, B&O Sound system, Interior, Head-up Display, Assistance Systems and schedule a test drive. They also had the option to watch the Audi video ad.</p>

<p>The gyroscope creative created an almost real life look & feel of the car interiors without the user being present at the store which could have been difficult to achieve by other mediums.</p>

<p>The campaign reached out to 400,000 unique users and the post click engagement was at 2.5% that is double the usual average of 1-2%, due to the interactive nature of the creative. The maximum engagement was seen in the age group 35-49 years of age. Audiences were more engaged with the campaign midweek compared to the weekends. Mornings and late evenings were preferred times for engagement during the day.</p>

<p style="margin-bottom:0px;"><strong style="color:black">Some of the insights that were found on the car features: </strong></p>
<ul>
          <li>Video was the preferred content consumption source for the audiences with around 11% post click engagement rate. </li>
          <li>The top 3 features explored by users were Navigation, Interior and MMI Touch. </li>
          <li>The least engaging features were B&O Sound and Head-Up display.</li>
          </ul>
