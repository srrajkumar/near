                <p>SINGAPORE, Near, a location-based mobile advertising startup with a presence in Singapore and India is eyeing expansion into Thailand, Vietnam, Japan and the US.</p>
<p>Japan is one of the largest markets for mobile marketing while Thailand and Vietnam are seeing rapid user growth for smartphones, said Shobhit Shukla, vice president of Near AsiaPacific.</p>
<p>The company's geolocation platform gathers location-based audience insights via a gamut of thirdparty applications without dependence on GPS or telecom operators.The information can help inform brands to better target advertising. Near's platform foundation is a hybrid geolocation system, which means it supports both smartphones and feature phones.</p>
<p>The company claims to deliver more than double the industry standard in engagement rates. Shukla cites the example of a campaign for Pizza Hut Delivery in Indonesia, which achieved a clickthrough rate as high as 1.51 percent.</p>
<p>Near doesn't limit its goal to helping brands reach audiences; it also provides potentialcustomer
insights. "Wesee ourselves as data evangelists, and all our effort goes into utilizing this data," Shukla said. Since the company's launch last year, Near's Singapore clientele has grown to include P&G, Unilever, CocaCola, Audi and SingTel. It now partners with more clients in Singapore, Indonesia, Malaysia, Philippines, Hong Kong, India and Australia.</p>
<p>Shukla did not divulge numbers, but said revenue has been doubling quarteronquarter and claimed that twothirds of its clients come back for more.</p>
<p>The company has reached 245 million user profiles in AsiaPacific and is growing at a rate of 1.2 million new users daily. It expects to reach more than half a billion profiles by the end of the year.	</p>
