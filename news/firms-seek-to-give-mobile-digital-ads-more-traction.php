											<p>In 2010, Anil Mathews, chief executive and founder of Near Pte Ltd, and his team travelled across the country to map various parts in a bid to create location-based advertising platform for mobile phone users. </p>
			    <p>Near did not use the global positioning system, or GPS, for online tracking, which most other advertising companies typically do. Instead, his company built its own platform to identify locations based on a combination of location data from mobile towers and Wi-Fi networks and relay location-centric advertisements to consumers.</p>
			    <p>Four years later, Near combines this platform with GPS and can now "...tell when a customer is near a McDonald?s burger outlet, or an Audi showroom and we show ads based on that. Burgers can be impulse purchases, but Near can tell if the customer can afford to shop for an Audi, based on the locality he lives in, which we figure based on the signal strength where he spends between 10pm and 6am every day. That is intelligent customer targeting", said Shobhit Shukla, vice-president, Asia-Pacific, Near.</p>
			    <p>The Singapore-based company uses more than 25 different types of ad formats, broadly categorized under rich media, video ads and dynamics ads to engage with consumers, and has about 100 million profiled users via 40,000 mobile apps across eight countries in the Asia-Pacific region.</p>
			    <p>Near claims that the current customer engagement rate for its ads - the number of customers that either interact with the ad itself, or visit the advertiser's website or showroom directly - is 41%.</p>
			    <p>Unable to ignore a market of over 156 million smartphone users, which is expected to double in 2014, according to research firm Mediacells, Indian companies are slowly waking up to this opportunity, trying to grab consumers? attentions by making more creative and engaging digital advertising for mobile phones.</p>
			    <p>"When I started out, almost half a decade ago, advertising was all about scale and footprints, but now successful advertising is about relevant customer targeting and retargeting. And, app advertisements are much better than SMS-based advertisements," said Shukla.<br>
