											<h4 style="text-align:left;"><?php echo $title; ?></h4>
											<p>Near, the location intelligence company, today unveiled its research on The Great Singapore Sale 2015. One of the key findings was that Orchard Road had the highest footfall amongst the locations covered under the study. The busy shopping district had almost 2.5 times more foot traffic, definitely in line with the fact that it has an array of malls on the shopping street, than Suntec City, followed by Raffles City and Bugis Junction. </p>

											<p>“Through our proprietary technology, we analysed foot traffic across 6 different areas in Singapore – Bugis Junction, Marina Square, Novena Square, Orchard Road, Raffles City and Suntec City – which allowed us to gain unique insights into the trends of Singapore Shoppers,” said Smriti Kataria, Director for Research and Marketing, Near. “With these powerful consumer insights, marketers will now be able to better understand how consumers move and behave when it comes to purchasing which can be used to implement strategies that increase their marketing effectiveness.”</p>

<p>Through the study, it was found that Orchard Road was mostly preferred by females, whereas Marina Square was seen as the preferred shopping destination for males. The report also showed that Sunday was the favourite day for a shopping spree among females, whilst men favoured a mid-week splurge, with footfall at its highest on a Tuesday. Despite the preference for different days, both genders chose to shop between 5pm and 9pm.  </p>

<p>While Marina Square had many people coming from various locations within Singapore, Orchard Road and Novena Square saw shoppers coming from its nearby locations. On the contrary, Suntec City, Raffles City and Bugis Junction had shoppers coming from all parts of Singapore. </p>

<p>Suntec City was the go-to shopping location for Working Professionals, while Homemakers ventured to Novena Square to get their shopping done. Students frequented Marina Square and Raffles City whilst Travellers flocked to Bugis Junction regularly. </p>

<p>Near found that the vast differences in preferred shopping locations was propelled by age. 16 – 25 year olds tended to shop most at Bugis Junction and 26 – 35 year olds chose Raffles city and Marina Square. Those aged 36 and above were inclined to shop at Suntec City. </p>

<p>Near’s The Great Singapore Sale 2015 research analysed foot traffic for the month of June among 192,000 users in 6 key locations in Singapore. Their previous research on <a href="<?php PDF('consumer-intelligence/japac-traveller-report-2014.pdf'); ?>" target="_blank"> JAPAC Traveller</a> analysed traveler and mobile engagement behavior across 12 airports in JAPAC region. In the recent past, Gartner named Near as a ‘2015 Cool Vendor in Consumer Dynamics’. </p>



