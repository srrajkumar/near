<h4 style="text-align:left; font-size:18px;">What it does</h4>
<p>Near is a mobile data platform that provides retailers with real-time information on consumer behaviour. The platform leverages a shopper’s location and interest to create data driven marketing messages for retailers, which are then sent to shoppers’ mobiles.</p>
<p class="img"> <img src="<?php IMAGE('news/audience-profiling.jpg'); ?>" alt="Audience Profiling" style="width:100%;" />
<em style="text-align:left; color:#000; font-size:12px;">Near is a mobile data platform that gives retailers insight into consumer behavior and location to create targeted marketing messages</em>
</p>

<p>“Near’s proprietary technology enables it to compile accurate user information without GPS or operator dependence,” says the start-up’s European general manager Ken Parnham.</p>
<p>“Our platform profiles mobile users by analysing location data points over time, coupled with user behaviour and context.”</p>
<p>The start-up provides retailers with insight into the statistics of who is shopping in their store to create customer demographics.</p>


<h4 style="text-align:left; font-size:18px;">What problem it solves</h4>
<p>Near combines various mobile data points, such as whereabouts, content consumption behaviour and points of interest, to generate real-time consumer insights.</p>
<p class="img" style="width: 35%; float: left; margin-right: 10px;"> <img src="<?php IMAGE('news/anil-mathews.jpg'); ?>" alt="Anil Mathews" style="width:100%;" />
<em style="text-align:left; color:#000; font-size:12px;">Near founder and chief executive Anil Mathews</em>
</p>
<p>Retailers can use these insights to intelligently target their consumers at the most opportune moment and drive conversions.</p>


<p>Data provided by Near also allows retailers to measure attribution to mobile and other media and then use this insight to drive broader business decisions.</p>
<p>“Consumers want to be able to shop anywhere, at any time” says Mathews.</p>
<p>“From the moment a customer expresses interest in a product, to purchase, retailers need to ensure they remain present and relevant throughout the path to purchase and Near’s technology helps them do just that.”</p>

