<p>Near, the leading location intelligence platform today announced the joining of Cameron Curtis as the General Manager for its Australia and New Zealand business. In his role, Cameron will be spearheading the ANZ business and will be responsible for scaling Near's data and marketing partnerships in the region. </p>
        <p>Cameron comes with more than 20 years of experience in the Australian digital media and technology landscape in leadership roles for The Sydney Morning Herald, Allure Media, Tapit and Booodl which helped him in learning and implementing a range of digital strategies as the industry evolved. </p>
        <p>"Near is a firm believer in the power of mobile and location data and how it can connect the digital with offline. Australia is a fast growing digital market and marketers are very progressive when it comes to adopting new technologies" said Shobhit Shukla, Co-Founder &amp; Chief Revenue Officer of Near. "With Cameron's diverse experience working with large digital media companies as well as running startups, we hope to strengthen our position as the most powerful location intelligence platform in Australia and New Zealand." he added.</p>
        
        <p>"Near has built a proprietary location technology and has seen massive adoption since its launch in Australia 12 months ago." said Cameron on his joining. "Its data driven approach to mobile marketing is unique and a key reason why over 100 brands in Australia are using Near's capabilities for mobile marketing as well as for gathering consumer insights and offline attribution. I couldn't be more excited to take this opportunity to the next level with our key partners in the Australian market." he added.</p>
          
        <p>Cameron's appointment as Near's GM for ANZ, shows company's commitment to the Australian market and reflects the growing opportunities in the location data space in Australia. Near works with key brands like Woolworths, Renault, Telstra, McDonald's, Vodafone & Ikea amongst others in Australia. Globally, Near has over 710 million mobile user profiles across Japan & Asia Pacific. It is headquartered in Singapore, and is backed by investments from leading venture capitalists Sequoia Capital, Canaan Partners, Telstra Ventures & Global Brain.</p>
