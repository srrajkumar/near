											<h4 style="text-align:left; margin-bottom: 20px;"><?php echo $title; ?></h4>
											
<p>Near, the largest location intelligence platform, today announced the global launch of its Mobile-first Audience Cloud, Allspark. Powered by the Near platform, Allspark leverages diverse public and proprietary data sets which enable brands and enterprises to visualise and analyse audience location, behaviour, demography and interest data to power strategy, marketing and operations.</p>

<p>Allspark enables brands and enterprises to understand the dynamics of their target consumers by leveraging diverse public and proprietary data sets, and curate audience segments based on historical and real-time data. Allspark allows its users to use data points like places, demographics, interests, and pre-built audience sets to curate audiences. Furthermore, Allspark empowers brands to activate these curated segments, optimize targeting strategies in real-time to maximize effectiveness, and also measure the impact of their spends using attribution analytics.</p>

             							 <p>Each audience in Allspark is represented by an AudienceCard. Real-time insights can be viewed for each AudienceCard including active audiences over a period of time, demographics and nature of these audiences. Once this audience is targeted around hand-picked locations, the attribution analytics in Allspark enables the user to compare real world conversions across locations. The user can also compare the digital and real world interests of the engaging audience, and can fine tune their targeting. The Near platform currently has a proprietary database of 65 million places which gets refined continuously.</p>
                                         
                                    
                                         <p>“Allspark redefines how brands gather insights about their consumers and interact with them in real-time. It enables you to measure and compare the impact of your business decisions across different audience &amp; locations, and optimise your investment,” says Anil Mathews, Founder &amp; CEO at Near. “We believe that Allspark with its state-of-the-art technology can provide brands with the power and knowledge to stay ahead in the market. Our aim is to ensure that data driven consumer insights are within reach for all, and utilized where they will be most valued.”</p>
                                          <p>“Allspark takes the complex world of real world and digital data, and demystifies it to make it accessible and actionable in a way like no other interface. It is a very intuitive product, and has already provided significant value to our clients,” said David Sandler, Head of Media Products at Fetch.</p>
                                         <p>Vishnu Mohan, CEO Asia Pacific, Havas Media Group commented, “Allspark from Near is a game changing product for our industry, and takes our data driven approach at Havas to another level. The interface is extremely simple to use and declutters complex data sets with ease, which can be actioned by brands in real-time. We look forward to getting started on Allspark and helping our brands with granular ‘real time’ consumer insights, which can be used to shape their business &amp; marketing strategies."</p>
										 
						 

 <p>Allspark has been available in beta to select marquee customers across geographies. Some of the early adopters of Allspark include Fetch &amp; Kantar in the United Kingdom, Havas &amp; Mindshare in South East Asia and MCN &amp; OOH Media in Australia.</p>


