											<h4 style="text-align:left;"><?php echo $title; ?></h4>
											<p>JAPAC's affluent are highly engaged on their mobile phones at the Sydney, Melbourne, Hong Kong, Macau and Jakarta airports according to the JAPAC Traveller Report 2014 released today by location intelligence company Near. Based on the average length of time spent across all airports covered, Singapore took the top spot at 97 minutes closely followed by Macau at 96 minutes and Bangkok at 91 minutes.</p>
                
<p>Shobhit Shukla, Vice President, Sales and Partnerships, Near commented, &ldquo;Through our proprietary technology, we were able to analyse over a million travellers across 12 cities in the region and grouped them into different user profiles. In particular, we found that affluent travellers in Sydney, Jakarta, Bangkok, and Kuala Lumpur airports were three times more engaged on their mobile phones compared to other travellers such as professionals, homemakers and students.&rdquo;
</p>
<p>Some other interesting trends were seen across the airports in terms of gender ratio, frequency of travellers, age group, and peak travel time. <br><br></p>
<h4 style="text-align:left;">Traveller Audience Demographic </h4>
<p>Australia had the highest proportion of female travellers at 28% while India the lowest at 17%. In Southeast Asia, Manila saw the highest proportion of female travellers at 27%. </p>
<p>Across all airports, the 25-36 age group were the most involved with their mobile phones. Conversely, the 37-50 age group had a lower level of engagement compared to other age groups due to smartphone penetration rates while the Japanese youth aged 16-24 were most engaged on mobile as compared to other countries.</p>
<p>As a major aviation hub, it is not surprising that Singapore travellers are the well-travelled in the region with 69% as repeat travellers travelling three times or more, followed by travellers in Sydney at 51%.  India has a high percentage of one-time travellers at 40%. <br><br></p>
<h4 style="text-align:left;">Mobile Phone Engagement </h4>
<p>Singapore travellers were the most engaged on their mobile phones, spending an average of 97 minutes on the device with homemakers being the most active, being five times more engaged than professionals.</p>
<p>Professionals in Sydney, Melbourne, Hong Kong and Kuala Lumpur were more engaged on their mobile compared to their counterparts in other countries.</p>
<p>Across all airports, 1600-1800 hours was the peak engagement time while Monday was the busiest day across all airports. </p>
<p>Seniors aged above 50 as well as those in the 37-50 age bracket had the highest mobile engagement in Bangkok while youth in Japan aged 16-24 were the most active on mobile.</p>
<p>Though smartphone penetration is lower in countries such as Thailand, Indonesia and India, users were more likely to use location-based services on their mobiles as compared to the high smartphone penetration countries in JAPAC. For developed countries like Australia, Japan and Hong Kong where usage of location-based services is lower, there is an opportunity for players who can geographically target users through proprietary technologies.</p> 
<p>"While this regional study concentrates on airports alone, we are able to help companies leverage our technology to target certain consumer profiles within a specified area to achieve offline consumer insights which are targeted, traceable, scalable and fully integrated with traditional marketing platforms. Marketers can also reach out to their audiences using our targeted mobile advertising solution," added Shukla.</p>

<p>Near's JAPAC Traveller Report 2014 analysed traveller and mobile engagement behavior of more than 1 million travellers across 12 airports in 9 countries in the JAPAC region during April-June 2014. The airports include those in Melbourne, Sydney, Hong Kong, Macau, Tokyo, Delhi, Mumbai, Singapore, Kuala Lumpur, Jakarta, Bangkok, and Manila. </p>
