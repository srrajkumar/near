<p><em style="margin-bottom: 20px;">After a weekend of eating and drinking, people in Singapore find Monday and Tuesday to be the most popular day to exercise, according to the report</em></p>

<p><a href="https://near.co" target="_blank">Near</a>, a Singapore-headquartered location intelligence platform, has today released an insightful report on the fitness trends and behaviour of people in Singapore.</p>

             							 <p>The research takes a deeper look into the different patterns of females and males at fitness centres across Singapore, helping to construct a better understanding of their general behaviour towards indoor exercise. With Singapore emerging as world’s fastest fitness hub, Near analysed over 37,000 unique users in 15 locations across 5 gyms – Fitness First, True Fitness, Anytime Fitness, Evolve and
Safra.</p>
                                         <p>The findings from the study will allow brands to understand the mobile behavior of fitness enthusiasts, enabling them to reach their relevant audience at the most opportune moment. As the fitness industry in Singapore grows exponentially, attracting many customers with its accessibility and affordability, businesses in Singapore should leverage on the health boom.</p>
                                         <p>This fitness study will provide insights into the customers’ fitness center preferences, breakdown of preferences by gender, peak time and day along with their content consumption.</p>
										 
<h4 style="text-align:left; font-size:18px;">Key findings from the research:</h4>		
<ul class="listing">
    <li>44% of people analysed in Singapore prefer True Fitness, with Anytime Fitness coming second at 28%</li>
    <li>3 out of the 5 gyms analysed found mornings to be the least popular time for a fitness Session</li>
    <li>All fitness centres analysed, except Safra, saw highest foot traffic between 4pm and 7pm</li>
    <li>More males (80%) were seen to frequent Evolve, while females (31%) were more in favour of Fitness First</li>
    <li>In general, males (75%) were more likely to visit fitness centres during this time period than females (25%) </li>
    <li>Early week workouts were more favourable, with Monday and Tuesday identified as most desirable. Wednesday and Sunday were the least popular choices.</li>
    <li>During workouts, a higher number of people in Singapore were on fitness apps at Evolve (28%), followed closely by Fitness First (23%)</li>
</ul>									 
 <p>This report identifies areas of improvement and signs of success for fitness centres in Singapore, helping construct insight into behaviour trends for females and males across the nation.</p>
      

<h4 style="text-align:left; font-size:18px;">Actionable Insights:</h4>

<ul class="listing">
<li><b>Marketing strategy and execution</b> - Plan marketing strategies and apply time-appropriate execution with the customers at the right time. For example, insights like content consumption, peak time and day could help brand marketers to drive awareness of their product offerings with the fitness enthusiasts group.</li>

<li><b>Business Expansion </b> - Potential for food and health stores to leverage the fitness trend by creating menu options and food products that are more health-conscious, and identify locations to stock these products.</li>
<li><b>Real-time targeting </b> - Plan marketing strategies and apply time-appropriate execution with the customers at the right time. For example, insights like content consumption, peak time and day could help brand marketers to drive awareness of their product offerings with the fitness enthusiasts group.</li>
<li><b>Competitor tracking </b> - Real-time intelligence helps in understanding the customer behavior of competitors, thereby enabling them to deploy strategies that attract competitor’s customers. In the study, it was observed that Males frequented Evolve while Females frequented Fitness First. This indicates that both the fitness centers should revisit their brand and go-to-market strategy to increase foot traffic of the opposite genders respectively.</li>
</ul>
