<p class="question" style="font-size:15px;">Q&amp;A: Near’s Ken Parnham on the Location Intelligence Opportunity</p>

<p>With the average consumer expected to own more than three connected devices in the next two years, location-centric marketing campaigns are set to be big business.   </p>

             							 <p>In fact, according to a Forbes executive at this year’s Mobile Location Leadership Forum, location-based ads will soon account for 40% of overall ad spend and the number of location-aware mobile applications will triple by 2019.</p>
                                
                                         
                                    
                                         <p>Having compiled 8.4 billion individual location footprints to create more than 700 million detailed profiles of global consumers, Near is a location intelligence specialist at the forefront of this marketing revolution.</p>
                                         <p>PerformanceIN touched base with the company's European general manager, Ken Parnham to discuss the scale of the location opportunity.</p>
                                         
                                        <p class="question" style="font-size:15px;">What opportunity does location intelligence present brands and businesses?</p>
                                         
                                        
                                         <p> <span class="question">Ken Parnham :</span> Location intelligence allows marketers to follow the complete customer journey, moving with them as they switch between the online and offline world. This means that marketers are able to identify consumers and ensure interactions are targeted to their requirements and position on the path to purchase.</p>
										 
					 
 
 <p>Location intelligence is not limited to campaigns either. The in-depth consumer understanding it affords can also be used to inform various aspects of business strategy, from reacting to trends and building audiences, to enhancing attribution precision.</p>
 <p class="question" style="font-size:15px;">How would you describe the marketing industry’s demand for new ways to utilise location data?</p>
 <p> <span class="question">Ken Parnham :</span> In the face of constantly evolving consumer tastes and behaviours, brands are looking for a data source that can keep pace. Location intelligence fulfils that need by producing a supply of real-time insights that is constantly updated.</p>
 <p>Our latest solution, Allspark, was born out of a demand for multiple data sets to help marketers pinpoint audience location, demographics or interests at any given moment, and instantly adjust their messages for maximum relevance. As technology and consumer habits develop, staying one step ahead is increasingly crucial to marketing success.</p>
 
 <p class="question" style="font-size:15px;">What advancements have been seen in this area in the last few years?</p>
 
 <p> <span class="question">Ken Parnham :</span> Location-based services are integral to almost every element of modern day life. Mobile applications such as Uber and TripAdvisor have made sharing location data easy and — crucially — they have increased our willingness to do so if it means we will receive something beneficial in return. As a result data volumes have grown exponentially, driving significant advances in audience analytics.</p>
 <p>When combined with the surge in smart device usage, the opportunities for brands to effectively engage audiences are endless. Retailers were among the first to embrace the technology, deploying insights to enhance real-time campaigns and improve measurement, but other sectors are beginning to follow.</p>

<p class="question" style="font-size:15px;">According to eMarketer, global smartphone users will reach the 2.5 million mark by 2018. How do you think smartphones have changed the expectations of consumers around their interactions with brands?</p>

<p> <span class="question">Ken Parnham :</span> Smartphone usage has become truly ubiquitous — the average UK consumer, for example, spends almost two hours each day on their mobile. This has transformed both the way audiences communicate and what they expect from brands. Everything is instant and personal in the mobile environment, and brand communication is therefore expected to be immediately relevant and tailored to match the specific dimensions of different devices.</p>

<p>Consequently, campaigns and strategies are more mobile-specific than ever — not just creatively, but also in their approach to targeting, and location data is increasingly being utilised to enhance impact.</p>

<p class="question" style="font-size:15px;">What are the challenges associated with location intelligence and how is the industry working to overcome them?</p>

<p> <span class="question">Ken Parnham :</span> The biggest challenges for location intelligence relate to data collection, consolidation and analysis. The influx of user data has created a wealth of information, but filtering through to extract actionable insights, remove inaccuracies, and identify individual users across data sources is difficult. </p>
<p>There are many methods of addressing these obstacles. Platforms can amalgamate and analyse insights from several data sources in real-time to produce a clear view of user journeys. They can also maintain precision by testing information at various stages to instantly detect discrepancies. At Near, for example, we use cartographic data and multiple source linkage to ensure accuracy.</p>


<p class="question" style="font-size:15px;">What advice would you give marketers looking to implement a location-based marketing strategy?</p>

<p> <span class="question">Ken Parnham :</span> Don’t underestimate location intelligence. Its ability to precisely place individuals in the moment means it’s often seen as a tool for real-time, location-dependent offers. But it’s much more than that.</p>
<p>By gathering <a href="<?php echo SITE_URL; ?>research/" target="_blank">insights</a> over time about a consumer’s habits, routines and interests, brands can build up a fully-rounded picture of who they are, what they want, and the products they like — enabling brands to craft messages that are uniquely relevant, serve them at the ideal time, and measure their responses across channels.</p>

<p class="question" style="font-size:15px;">In your opinion, what does the future hold for location intelligence?</p>
<p> <span class="question">Ken Parnham :</span> Location intelligence will grow significantly across all sectors. The insights of individual activity and behaviour it generates is gathering the interest of governments, for instance, as a tool to improve transport and city planning.</p>

<p>Health care too is beginning to realise its potential for managing epidemics and building hospitals. Then there are its uses as a fraud prevention mechanism to check transactions against location and as a means to connect with the Internet of Things (IoT) to control energy efficiency in buildings by switching off lights and heating remotely.</p>


