											<h4 style="text-align:left; margin-bottom: 20px;"><?php echo $title; ?></h4>
											
<p>The yearly arrival of Cannes Lions is hard to miss. La Croisette transforms into a Silicon Valley outpost with super-sized ad tech yachts lining the French Riviera – perfectly demonstrated by Terence Kawaja’s Cannes 2016 yacht <a href="https://twitter.com/tkawaja/status/744989255018119168" target="_blank">LUMAscape</a> – and rosé consumption skyrockets.</p>

<p>This week, when they weren’t battling with <a href="http://www.adweek.com/news/technology/cannes-lions-attendees-are-crying-foul-over-wi-fi-woes-along-croisette-172161" target="_blank">Wi-Fi issues</a> or pausing to <a href="http://digiday.com/platforms/snapchat-everywhere-nowhere-cannes/" target="_blank">Snapchat</a>, the world’s leading advertising and tech experts were discussing the trends tipped to transform the industry in the next 12 months.</p>

             							 <p>So which topics stood out? Here are four key takeaways:</p>
    <h4 style="font-size:18px; text-align:left;">‘Bad’ advertising is dead</h4>                                     
                                         
                                    
                                         <p>Ad blocking was everywhere — discussed by everyone from Piers Morgan to Iggy Pop. But this year we moved from simply talking about the problem to looking at solutions, and it seems the industry is determined to stop the technology arms race of blocking software and anti-blockers by eradicating bad ads.</p>
                                         <p>Discussions cited one of the chief causes of consumer dissatisfaction as the growing volume of irrelevant and intrusive ads, and emphasised the importance of delivering quality. It was almost universally acknowledged that targeted ads can evoke positive responses, but only if the industry raises its game.</p>
                                         
                                        
                                         <p>Attendees agreed that marketing must be focused on adding value to the user’s digital experience, with suggestions to improve relevance including the use of mobile location data to build a complete picture of individuals and ensure marketing messages can be adapted to them in real-time.</p>
										 
		<h4 style="font-size:18px; text-align:left;">Fears and hopes for artificial intelligence (AI)</h4>				 
 
 <p>AI has crossed the borders of sci-fi to become a reality and now that computers can think without our help, many are pondering the implications for the future of humanity.</p>
 <p>The buzz around AI at Cannes was all about the opportunities it presents to bring greater efficiency to our chaotic modern world. During a presentation from Google Chairman Eric Schmidt, for example, we heard that AI is destined to address issues such as population growth and climate change by deploying the abilities of hyper-intelligent robots.</p>
 <p>The overriding message of the week was that we are not about to become slaves to robots.</p>
 <p>Indeed, conversations were squarely focused on the ways in which AI is already starting to improve the scale and impact of marketing messages by joining forces with customer data. From smart tools that can deliver personalised experiences — using factors such as user location, age and gender — to online bots that streamline customer services by answering user questions in live chats, AI is fast becoming a valuable and effective marketing tool.</p>
 
 <h4 style="font-size:18px; text-align:left;">Next steps for virtual reality (VR)</h4>

<p>Although VR is not a new trend for discussion at Cannes, its recent eruption into the mainstream means that it is finally ready to kick-start an entertainment revolution — and that makes it a hot topic.</p>
<p>With headsets like the Oculus Rift, HTC Vive and Samsung Gear VR now available to buy, and Google cardboard offering an affordable means to test VR, the technology is poised to change the way consumers interact with and access content. While the initial tremors are most likely to be felt in the gaming world, the experts at this year’s festival left attendees in no doubt that the wider sphere of online entrainment is next, and the time to prepare is now.</p>


<h4 style="font-size:18px; text-align:left;">Calls for gender equality intensify</h4>

<p>The tech industry has long suffered with a significant gender imbalance that many leading advertising and marketing figures are attempting to redress, and the subject was high on the Cannes agenda. Yet as with any complex issue, progress is mixed and industry-wide alignment is still crucial to reach a solution. As several thought leaders called for greater recognition of female achievement, there were still Cannes Party invites seeking <a href="http://www.adweek.com/news/advertising-branding/gary-vaynerchuk-apologizes-cannes-party-invite-seeking-attractive-females-only-172163" target="_blank">'Attractive Females Only'</a>, which — quite rightly — received a very negative media response.</p>
<p>One of most notable presentations arguing for greater gender equality was executive Madonna Badger, who delivered a passionate speech about why ads that objectify women are not just damaging female self-perception, but also brand reputations and profitability.</p>
<p>Encouragingly, global corporation Unilever was also keen to declare its support for the cause — vowing to stop stereotyping women in ads and ensure 400 of its brands, including Dove, Cif and Surf, fully embody the new edict.</p>
<p>So as the ad tech yachts weigh anchor and Cannes Lions closes for another year, we have learnt that campaign relevance will be vital to fight the blockers, AI is due to enhance — not replace — marketing, VR is the next big thing on the tech agenda, and there is no place in modern ads for gender stereotyping. Now let’s see how these trends pan out in the year ahead.</p>

