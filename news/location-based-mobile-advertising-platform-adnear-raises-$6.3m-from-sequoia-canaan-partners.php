											<h2 style="text-align:left;"><?php echo $title; ?></h2>
											<p>Near, a location-based mobile advertising platform that supports both feature phones and smartphones, has raised Rs 35 crore ($6.3 million) in Series A funding from Sequoia Capital and Canaan Partners. The money raised will be primarily used to support the company’s expansion plans across the Asia-Pacific region and also to expand the team.</p>


        <p>
“The market in India is not as mature as countries like Australia. The funds will be mostly utilised to expand our operations in other APAC countries. We are planning to open two sales offices in Mumbai and Delhi, and another office in Australia in the next six months,” said founder and CEO Anil Mathews. The money will be also used to increase the team size from the current 20 to 60 in the next six months.</p>
        
        <p>Singapore and Bangalore-based Near Pte Ltd (formerly Imere Technologies) was founded by Mathews back in 2009. The company’s advertising platform is built upon a proprietary hybrid geo-location platform, which helps provide location awareness on mobile phones without GPS or operator assistance. The company is currently servicing clients in India, Singapore, Australia and other Asia-Pacific countries. Its key clientele includes brands and advertisers like Toyota, Titan, Ford, PizzaHut, Samsung, Nokia and Airtel, among others.</p>
        <p>Commenting on the investment, Rahul Khanna, MD of Canaan India, said, “Near’s technology enables advertisers to deliver hyper-targeted ads on mobile devices. We are excited to support the company and look forward to leveraging our expertise in the digital media domain to support Near’s international expansion.”
        </p>
        <p>According to Mathews, the company is the pioneer in location-based mobile advertising and helps advertisers with 4-5 times more impactful and engaging campaigns, thanks to its proprietary location data.</p>
        <p>The company leverages real-time geo-location, combined with consumer behavior, to target relevant users within a geo-fence. As a result, advertisers are able to reach a massive audience, based on where people are located or what’s actually near them. All ads are displayed within premium mobile apps and ensure further engagement as users can find their way to the advertised stores, use coupons, call the businesses and so on.</p>
        <p>"Near offers a unique opportunity for brands to engage with their consumers in a relevant yet non-intrusive way. After establishing the platform in India, the company is now ready to expand globally," said Mohit Bhatnagar, MD at Sequoia Capital India.</p>
        <p>Canaan’s current technology investments in India include BharatMatrimony, Naaptol and UnitedLex while Sequoia has invested in companies like Café Coffee Day, Idea Cellular, Just Dial, Manappuram Finance, Paras, Quick Heal, Micromax, Mu Sigma and Vasan Health Care.</p>
	
