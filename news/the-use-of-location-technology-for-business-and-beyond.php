<p>In his Q&amp;A, Shobhit Shukla, Co-Founder &amp; Chief Revenue Officer at Near talks about the use of location technology for business &amp; beyond.</p>

<p class="question" style="font-size:15px;">What does location technology mean to Near?</p>
<p>In today’s data-rich world, the number of ways location technology is helping businesses is enormous. Location technology is empowering businesses with rich consumer insights that help them make smarter marketing and business decisions. By leveraging it, businesses can understand consumers & their dynamics in real-time, measure attribution analytics, competitor insights, deploy relevant and effective marketing strategies impacting their sales and returns.</p>
<p>Location technology has great potential in urban planning. Location data can help Governments and Urban Planners with predictive analytics to assess traffic movements within the cities and decide where to widen lanes or install new traffic lights, to ascertain where new parking lots or meters should be constructed and where new facilities like hospitals, schools etc should be built for public benefit. Furthermore, location data can impact everything from transportation, pollution control, waste management to land usage.</p>



<p class="question" style="font-size:15px; margin-top:20px;">What are the factors driving adoption of location technology?</p>
<p>Today, 80% of the data derived from mobile devices comes with a location component. With smartphone users <a href="https://techcrunch.com/2015/06/02/6-1b-smartphone-users-globally-by-2020-overtaking-basic-fixed-phone-subscriptions/" target="_blank">growing</a> at a rapid pace and also with the increase of wearable, virtual reality, augmented reality and Internet of Things (IoT) devices, the data that these devices produce will be massive. Businesses are realizing the importance of real-time insights because of the ever-connected consumers. With multiple vendors, there is an increasing need for brands to converge multiple data sets from data streams to create a unified view of the consumer and hence create meaningful experiences for their consumers.</p>


<p class="question" style="font-size:15px;">Cite a few examples of how location technology is helping your clients?</p>
<p>Near’s platform has been utilized across many businesses globally. We ran a really interesting campaign with Shell in the Philippines where we used location data to promote products and services to consumers in both its own and competitor stores. We saw that location data not only helped Shell to gather rich insights about consumers and target them at the most relevant moment, but it also helped them to measure impact. Shell saw a <a href="https://near.co/media/pdf/latest-research/shell-competitor-targeting-consumer-insights.pdf" target="_blank">14%</a> increase in foot traffic, with 20% of this jump coming from the competitor outlets.</p>

<p>In another example, we helped one of the leading fitness clubs in London to increase footfall by 76% during and post the campaign using location tech. Location data also helped us in understanding the kind of audiences walked in post their engagement with the brand, analyze which media influenced their purchase behavior, measure spend propensity and measure impact of the marketing effectiveness.</p>


<p class="question" style="font-size:15px;">What is your focus at Location World 2017? Can you give some insights into what you will be talking about?</p>
<p>At Location World 2017, my presentation would focus on the factors that will impact location technology in 2017. It would include data technology trends like Augmented Reality, IoT, mobile and how the convergence of data streams using products will help retailers and brands to optimize returns and bridge the gap between online and physical worlds.</p>



