                                            <h5>The marketing intelligence entrepreneur depends on his social network for tech news</h5>
											
<p><em>Shobhit Shukla, our Co-Founder & Chief Revenue Officer in his latest interview with The Financial Times</em></p>

<p>I travel for work two or three weeks every month. It’s a lot of travel, and I’m not at home in Singapore as often as I would like to be. </p>
<p>We are a marketing intelligence technology company, and we have offices in London, Sydney, Bangalore, San Francisco and Tokyo. Bangalore is a four-hour flight from Singapore but all the other offices are between eight and 14 hours away, San Francisco is almost 20.</p>
<p>The most important thing in a start-up is to make sure all members of the team are alive to the culture. I spend as much time as possible sharing new ideas and products. Feedback flows from the bottom up. But sometimes that feedback gets lost in noise, so I have to go to meet the staff myself. That’s the main reason why I travel so much.</p>

             							 
                                         <h4 style="font-size:18px; text-align:left;">The kit</h4>
                                         
 <p>All this travel comes with the added pressure of having to make the most of my time. Five years ago I would take a book, a laptop and a newspaper on a long-haul flight. Today it’s tech: Kindle for reading, iPad for all audio and visual content and a laptop for work. I don’t get jet-lagged and I try to time it so I arrive in cities in the second half of the day, then go straight to sleep.</p>
 
 
 										<h4 style="font-size:18px; text-align:left;">The podcasts</h4>
 
 
<p>Podcasts are interesting when they give you the take of a few fascinating individuals. I’ve always been a big fan of National Public Radio and I never miss their Planet Money podcast. I like the presenters and their chemistry, but also the way they curate content.</p>
<p>I also love the a16z podcast by Andreessen Horowitz, the venture capital firm. They look at how technology has come about and ask how we will be living in the next 20 years.</p>
 
 
 <img src="<?php IMAGE('news/the-podcast.png'); ?>" style="width: 100%;"  alt="The podcasts" />
 
 
 <h4 style="font-size:18px; text-align:left;">The memoirs</h4>
 
<p>I’ve never been able to read novels. I like biographies because I find them inspiring. I’ve just finished Nike founder Phil Knight’s memoirs, Shoe Dog. I also like books on global economics, such as Ruchir Sharma’s The Rise and Fall of Nations.</p>
 
 
  <h4 style="font-size:18px; text-align:left;">The news</h4>
 
<p>I rely on Google News and the BBC — but most stories of importance to me are aggregated on Google News and I get my fix for the day in the first half hour. </p>
 
 
 <h4 style="font-size:18px; text-align:left;">The network</h4>
 
<p>I’m a big fan of user-generated content. There was a time a few years ago when I checked Google Reader, and I always read Mashable and tech blogs, but I’ve realised my behaviour has changed.</p>
<p>Because I work in technology a major source of that information has shifted to LinkedIn and I’m a heavy user. I have a large circle of people with whom I am doing business, I’m directly connected to them. The moment something happens in the world that has an impact on my network — an initial public offering, perhaps — it cascades immediately. It’s the de facto place to consume professional content.</p>
 
 
  <h4 style="font-size:18px; text-align:left;">The app</h4>
 
<p>I love an app called Quora, which I have been using more than anything else for the past couple of years.</p>
<p>It’s a microblogging app — you ask questions about finance and business, and you receive answers. What’s great is that I can trust the content because I know who the people answering me are, and I know their background. News articles are a collection of facts, but Quora is a mixture of facts and opinion — it’s a fascinating spectrum.</p>
 
 
 


