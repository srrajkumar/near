											<h2 style="text-align:left;"><?php echo $title; ?></h2>
										<p>This week Econsultancy’s Day in the Life feature focuses on Ken Parnham, who is the General Manager at location intelligence platform, Near.</p> 

<p>Let's find out what his role entails, as well as what it is like to work in such a nascent part of the industry.</p>

<h4 >Please describe your job: What do you do? </h4>

<p>Being Europe’s general manager for Near is a big responsibility, but it also gives me a first look at how emerging developments in location-based technology are changing the way we live.</p>

<p>Essentially, the buck stops with me for all new business opportunities and operations in the region, which means I cover everything from sales and marketing to account management.</p>

<p>It’s my job to boost efficiency, stay ahead of location intelligence trends — and competitors — and most importantly, ensure revenue is always optimised.   </p>

<p class="img" style=" width: 40%; float: left; margin-right: 20px; padding: 10px;    margin-top: 27px;background-color: #FBFBFB;"> <img src="<?php IMAGE('news/ken.jpg'); ?>" alt="Ken Parnham" style="width:100%;" />
<em style="text-align:left; color:#000; font-size: 11px; line-height: 16px !important; margin-top: 14px; display: block;">Ken Parnham, General Manager, Europe, Near</em></p>

<h4 >Whereabouts do you sit within the organisation? Who do you report to? </h4>

<p>I’m directly involved with multiple everyday functions and higher level strategy, which puts me right in the middle of things.</p>

<p>My position is a vital link between our business in Europe and the wider world, so I work closely with the Chief Revenue Officer, who I also report to.</p> 


<h4 >What kind of skills do you need to be effective in your role? </h4>

<p>Agility and listening are essential in an industry that evolves as rapidly and as often as technology. </p>
<p>There’s a constant flow of new devices, apps and concepts that alter consumer behaviour, so the ability to quickly understand and cut through the complexity is invaluable. </p>
<p>Communication is also crucial to maintain momentum. As part of a global organisation, my team needs to be completely aligned with the rest of the business and working towards the same core goals, which means I need to keep them informed and on track.  </p>


<h4 >Tell us about a typical working day</h4>

<p>Digital technologies are by nature ever-changing and two days are rarely the same, but a good day is a frequent occurrence. </p>
<p>On good days, my schedule might run something like this: an early start to answer urgent emails, tackle larger strategic issues and liaise with our headquarters in Singapore, then head into the office to catch up with my team and run through a pitch scenario, followed by a meeting with an existing client in the afternoon.  </p>
<p>Exploring ways to expand our business and better meet client needs is an integral part of what I do, so refining pitching skills to make sure prospects see what our technology can do for them and checking in with clients to understand what they need are very important.</p>

<img src="<?php IMAGE('news/location-intelligence-platform.png'); ?>" style="width: 100%;"  alt="" />
 
<h4 >What do you love about your job? What sucks? </h4>
<p>I feel privileged to be working in an industry at the vanguard of digital innovation.</p>
<p>Mobile data and the insights it generates are creating new possibilities in every sector — location intelligence is already improving targeting efficiency in retail and marketing, alongside healthcare, city planning, and government-level decisions.</p>
<p>It’s incredible not just to be part of this revolution, but also to have been there from the beginning. </p>
<p>Like any job, there are things that aren’t perfect, but fortunately there aren’t many of them. Sometimes not having as much time, as there are new avenues to explore, can suck. </p>


<h4 >What kind of goals do you have? What are the most useful metrics and KPIs for measuring success? </h4>

<p>There are three key metrics I gauge success by: revenue, market disruption, and workplace culture. </p>
<p>Revenue is, of course, a priority for any business but I strive to ensure the majority of it comes from repeat business — not only because this it makes for a sustainable inflow but also because it means we are delivering what our clients want, which is what matters most. </p>
<p>Creating disruption and a good working environment go hand in hand. If my team have room to build their skills and are passionate about what they do, our offering will continuously improve, helping us to outpace competitors and influence the global marketplace. </p>


<h4 >What are your favourite tools to help you to get the job done?  </h4>

<p>My team are unquestionably the best weapon I have. They are experts in their field who are not content with ‘good enough’ — constantly striving to push boundaries, perfect our services, and find new ways to meet client challenges.</p>
<p>Sophisticated technology is a necessity, but having a team that wants to get the best out of it is what inspires me.</p>
<p>I believe you should never forget to take time with your recruitment policy; your people will be the foundation of your success.  </p>


<h4 >How did you get started in the digital industry, and where might you go from here?  </h4>

<p>I’ve always been fascinated by digital technology and its potential to streamline and enrich our everyday lives.</p>
<p>About 18 years ago, I decided to develop my proficiency with the tools of the trade by becoming a computer programmer. I haven’t looked back since.</p>
<p>The beauty of this industry is that you never know where it’s going next, but whatever comes next, it’ll be too exciting to miss, so I hope to be there at the centre of it all.</p>

<h4 >Which brands do you think are doing digital well?  </h4>

<p>Digital is such a fundamental element of branding now that examples of good usage are everywhere, but if I had to pick I’d say the <a href="https://econsultancy.com/blog/63577-how-virgin-used-big-data-to-inform-its-new-content-strategy/" target="_blank">Virgin Group</a> and <a href="https://econsultancy.com/blog/67541-10-delicious-digital-marketing-campaigns-from-mcdonald-s/" target="_blank">McDonald’s</a> do it especially well.</p>
<p>As international, recognisable brands you might not expect them to spend time trying new digital technologies. But there is a reason they are at the top and it’s because they keep pushing the envelope and embracing digital advances.</p>
<p>It impresses me every time I meet with them. </p>

<h4 >Do you have any advice for people who want to work in the digital industry? </h4>

<p>Ask questions all the time. The digital industry doesn’t stand still; the number of providers, technologies, sectors and trends it contains is always expanding, and your knowledge base needs to grow with it.</p>
<p>It can be hard work, but the rewards make it more than worthwhile. Dive in! </p>




