											 <p>With consumers generating more data than ever before, particularly through mobile devices, more marketers in Asia-Pacific are tapping big data analytics to improve attribution and create a better audience experience. Shobhit Shukla, Co-Founder &amp; Chief Revenue Officer of location-based data analytics provider, Near, highlights the challenges from this changing landscape and discusses how brands can differentiate their services with real-time insights.</p>
											 
<h4 style="text-align:left; font-size: 18px;">What are some key developments in the way user data is collected today?</h4>
<p>Few things are more valuable to brands than user data. It influences everything businesses do, from their media buys to the development of their user experience platform. But what happens when all that data, which is traditionally collected via cable or DSL connections, is disrupted by mobile technology?</p>
<p>Today’s shoppers are increasingly gravitating towards mobile devices. By 2019, the market share for smart devices – both 3G and 4G – is expected to grow by as much as 127% and account for 24.3 exabytes in data traffic per month. Handset prices also are decreasing, driving more people to use their mobile devices as the primary method for accessing the internet.</p>
<p>Mobile data will soon become the main source of user data, even as consumers split their time between their desktops, tablets, and smartphones. This boom in mobile data is a goldmine for marketers, but it does present some new challenges as well. Mining mobile data means gaining access to offline data and relying less on cookie-based technology. For mobile data to be effective, marketers will also need the ability to derive real-time insights of their users, which is currently beyond the scope of many third-party data providers.</p>

<h4 style="text-align:left; font-size: 18px;">How can brands address these challenges, for example, how do they access offline data and mine mobile data?</h4>

<p>Brands can leverage location data provided by mobile platforms. A brick-and-mortar brand can tap this data to understand what kinds of people are visiting its stores and what they are doing when they are not in the store. Are they working somewhere or studying at a university? Are they stay-at-home moms? Mobile data will become a ‘source of truth’ about consumer behaviour and mining this data will give brands valuable insights.</p>

<p>To mine data from mobile devices, we have to look at technology that does not rely on cookies. The most prevalent and persistent denominator for mobile measurement is IDFA for iOS and Google ID for Android. Both have made their respective identifiers user-friendly and privacy-compliant. And, unlike cookies, mobile identifiers are more persistent and, hence, enable data audience curation and measurement to be a strategic and long-term endeavour.</p>

<h4 style="text-align:left; font-size: 18px;">You mentioned that third-party data providers are unable to provide real-time insights. How then should marketers gain access to these?</h4>

<p>There is increasing activity from which real-time insights can potentially be curated, especially with people spending more time on their smartphones and having access to higher data speeds. Vendors like Near focus on leveraging user-specific location data at scale to gain such insights. For instance, we are currently working with a large retail chain to help them understand their customer behaviour and compare it with their competitors’ customer behaviour. They are using this not just to support their digital marketing efforts, but also for their offline marketing as well as events planning.</p>

<p>Without real-time insights, marketers will not be able to measure mobile campaign attribution. In fact, location technology is at the moment the only proven method to establish mobile campaign attribution. For example, location intelligence provides the real-time insight that allows marketers to accurately determine whether a customer’s purchase is due to their in-app mobile advertisement.</p>

<h4 style="text-align:left; font-size: 18px;">Doesn’t this pose a big challenge to marketers that want insights from massive volumes of data, including mobile?</h4>

<p>Big data brings up its own set of challenges for marketers, one of which stems from its complexity. So there is a learning curve with regards to its uses and operations. Another issue is the need to merge existing data with new data sources, resulting in issues with integration and this further complicates the already complex process of tapping big data.</p>
<p>Also, given the relative novelty of big data, not many skilled professionals are well-versed with the expertise required to handle it.</p>
<p>Another key challenge, which is very relevant to Asia-Pacific, involves the multitude of languages. This has proven to be a significant technology barrier in deploying big data across the region. One way of addressing this is to invest in extraction engines that can extract data across as many Asian languages as possible.</p>

<h4 style="text-align:left; font-size: 18px;">How has this changing landscape impacted attribution, which has been a topic of discussion in the industry?</h4>

<p>There is a big opportunity in offline attribution, which involves measuring the efficacy of digital spend in offline store visits. Location footprints mapped to device identifiers is a compelling way to measure ROI on digital and ‘Out of Home’ spend by brands.</p>
<p>For instance, if we see that a particular device has been exposed to an advertisement by a fast food chain, either directly on the phone or in the vicinity of a billboard, we can then map it to determine if this device was later seen at one of the fast-food chain stores. We’ve tested this model with various partners, such as Shell, which saw a 14% increase in visits to their gas stations as a result.</p>
<p>Asia-Pacific, interestingly, is taking a big leap in terms of user adoption, discovery, and transaction via mobile devices. Most of our work has been in Asia, so far, which is testimony to the growing needs for data and attribution amongst marketers here.</p>

<h4 style="text-align:left; font-size: 18px;">What’s the best strategy for collecting meaningful first-party data?</h4>

<p>This is something marketers need to invest in and there are challenges around privacy as well. It’s also hard to scale first-party data beyond a point, especially on mobile, as users are spending most of their time consuming specific content like games, entertainment, social networking, and video, and not necessarily the downloading of brand-specific apps.</p>

<h4 style="text-align:left; font-size: 18px;">Does this mean Asia-Pacific marketers should focus on first-party data, instead of third-party data?</h4>

<p>You have to be open to both. It’s the perennial argument of scale versus accuracy. You need to start investing in both, but since there’s already so much third-party data available and waiting to be tapped, that would be the obvious first step.</p>

<h4 style="text-align:left; font-size: 18px;">Can you provide some examples of how emerging technologies such as the Internet of Things (IoT) can enable marketers to offer a new level of personalisation?</h4>

<p>Marketers will be able to differentiate their offerings, achieving a whole new level of personalisation. They can analyse the buying habits of their customers across various platforms, as well as gain deeper insights into the exact location of a customer throughout the buying journey, at any given point of time. With this data, marketers will have access to previously unattainable data about how consumers interact with devices and products; which, in turn, offers a clearer look at how the customers interact with the brand as a whole.</p>
<p>With the ability to attain real-time, point-of-sale notifications, and targeted ads, marketers can be assured the insights they possess are personalised and particular to their customers.</p>

<h4 style="text-align:left; font-size: 18px;">What challenges does IoT present to marketers?</h4>
<p>One would involve making the switch from ads to experience. As IoT progresses, consumers will be flooded daily with multitudes of videos and ads. In order to emerge amidst the noise, marketers will need to personalise their offerings. Gone are the days of generic ads. It’s all about ensuring your message is audience-specific, offering your customers an experience with the brand that resonates over time.</p>
<p>Furthermore, as mobile technology evolves, future screens will become smaller, more functional, and task-oriented. As such, marketers need to get more creative to ensure their ad or message is optimal for these screens.</p>
<p>And with the move towards data, marketing and data science are slowly but surely merging. Instead of looking at it as two different skillsets, the industry needs to evolve to incorporate both aspects. It is not a question of whether marketers should also be well-versed in data science, it is more a question of when.</p>



