											<h4 style="text-align:left;"><?php echo $title; ?></h4>
											 <p>Affluent in Australia are seen travelling within the country to attend the Australian Open & F1 Grand Prix, according to the study released today by location intelligence company Near. The study analysed 4 events in Australia that were held last year: The Australian Open, T20 cricket match, Eminem Live Concert & the F1 Grand Prix. All the event venues show an increase in footfall compared to the regular days, but it is very interesting to see the analysis of the incremental count.</p>
<p>Australian Open saw 5x increase in footfall during the event, the highest amongst all other events, followed by the Eminem Concert which saw 4.5x footfall during the event. While the foot traffic increase in Australian Open came from males, the increase in the Eminem concert came from females that saw a 7x jump.</p>
<p>Shobhit Shukla Vice President, Sales and Partnerships, Near commented, &ldquo;In our study we see clear preference for the Australian Open amongst men and for the Eminem concert amongst women last year. We analysed over 100,000 users through our proprietary technology, to understand the footfall trends and mobile audience behavior during the events conducted last year. The study provides powerful insights on event preferences amongst various audiences, and can help marketers reach out to their target audience in the offline & digital world.&rdquo;</p>
<p>While F1 was the top choice for travellers, affluent and professionals were keen on the Australian Open. The Eminem concert was popular amongst students & homemakers. Users aged between 25-36 years preferred the T20 cricket match, the ones above 36 years of age preferred the F1 Grand Prix while the younger age group between 16-25 years preferred the Eminem music concert. While the iOS users were seen primarily at the Australian Open & F1 Grand Prix, the android users were found at the T20 and the Eminem concert.</p>
