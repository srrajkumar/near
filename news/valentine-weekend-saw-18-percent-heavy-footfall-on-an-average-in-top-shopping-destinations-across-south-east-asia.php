											<h2 style="text-align:left;"><?php echo $title; ?></h2>
											<p>There was a significant rise in the number of shoppers during the weekend of Valentine's in Hong Kong, Indonesia, Jakarta and Singapore as compared to the Chinese New Year, Christmas and New Year weekends, according to the latest research by Near, the leading location intelligence company in the region. While shopping locations in Singapore saw a 25% increase in footfalls, Hong Kong and Jakarta experienced a 15% increase in footfalls on the Valentine's weekend. Relative to these countries, Kuala Lumpur in Malaysia saw 30% higher footfalls during the weekends of Christmas and New Year weekends. </p>
                <p>Near studied shoppers across the key shopping locations in Singapore, Jakarta, Kuala Lumpur and Hong Kong during the weekends of Christmas, New Year, Valentine's and Chinese New Year. The research showed that shoppers in Jakarta spent 50% more time shopping on these weekends compared to shoppers in Singapore. More men went shopping during Valentine's weekend in Singapore, Hong Kong and Jakarta, while more women shopped during New Year and Christmas weekends. </p>
                <p class="img"><a href="<?php PDF('consumer-intelligence/consumer-trends-during-chinese-new-year-across-sea.pdf'); ?>" target="_blank"><img src="<?php IMAGE('news/cnyimg.png'); ?>" style="width:100%; height:auto" alt=""/></a></p>
                <p>Amrita De La Pena, the regional director for South East Asia, Near comments, "Our location and mobile audience data pool across Japan and Asia Pacific enables us to come up with shopper insights which would be difficult to obtain at scale through any other medium but mobile. The actionable insights derived from our data points can be used by brands for understanding their consumers better and reaching out to them."</p>
                <p>"It is interesting to observe that, there was a lot of quick shopping during the Valentine's weekend at Singapore and Kuala Lumpur, while shoppers had more time at hand during the Christmas weekend, now brands can accordingly plan their merchandising and retail promotions during these weekends." added Amrita.</p>
                <p>Among the shoppers, Affluent and Homemakers were seen spending the most time at the shopping locations in all the cities. Some of the other city-wise interesting trends seen during the weekends covered under the study:</p>
                
                
                
                <h4 style="text-align:left;">Singapore</h4>
                <p>Shoppers in Singapore spent more time during the weekends of Chinese New Year and Christmas as compared to that of New Year and Valentine's. While increase in footfall for women were seen during the New Year weekend, the increase in footfall for men was evident during the Valentine's weekend.</p>
<p>China Town and Marina Bay Sands saw high footfall on Friday's for all weekends except Chinese New Year. However, Saturday remained the top choice for many shoppers in those locations. Orchard Road had very high footfall during Saturdays on Christmas, New Year and Valentine's and Sunday on Chinese New Year.</p> 
<p>While shoppers in the age groups of 16-24 and 36-50 were actively shopping over the Valentine's weekend, seniors aged above 50 were the most active shoppers during Christmas weekend. Similar trends were seen across all the shopping locations in Jakarta and Hong Kong.</p>
<p>Shoppers were seen spending the most time in China Town followed by Orchard Road and Marina Bay Sands. They were most active during the weekends of Chinese New Year followed by Christmas, New Year and Valentine's. </p>

<h4 style="text-align:left;">Hong Kong</h4>
                <p>Tsim Sha Tsui, Mongkok and Causeway Bay saw heavy footfall during the Valentine's weekend as compared to other weekends. While the increase in footfall for women was seen during the New Year weekend, footfall increase was high for men during the Valentine's weekend.</p>
<p>The time spent by the shoppers was highest during the Christmas weekend followed by the weekends of Valentine's, New Year. Shoppers spent least time during the Chinese New Year weekend with almost 50% drop in time spent of the Christmas weekend. Mongkok had the highest footfall followed by Tsim Sha Tsui and Causeway Bay.</p>
<p>Shoppers in the age groups of 16,24 and 25,36 were most active during the Valentine's Weekend, while those above 36 years preferred the Christmas weekend the most to shop. Fridays had the highest footfall during the weekends of New Year's and Valentine's, while Sunday saw heavy footfall during Chinese New Year weekend and Saturday for Christmas weekend.</p>

<h4 style="text-align:left;">Jakarta</h4>
                <p>Footfall increase was high amongst both men and women during the Valentine's weekend as compared to the others. Time spent by shoppers was high on the weekends of Valentine's followed by New Year, Chinese New Year and Christmas.</p>
                
                
                <h4 style="text-align:left;">Kuala Lumpur</h4>
                <p>Footfall increase for women was high during the Christmas and Valentine's weekend, while footfall increase for men was seen during the New Year weekend. Shoppers spent more time during the New Year weekend as compared to other weekends.</p>
<p>Near's study analysed thousands of users seen in major shopping locations - China Town, Marina Bay Sands, Orchard Road in Singapore, Causeway Bay, Mongkok, Tsim Sha Tsui in Hong Kong, Grand Indonesia, Plaza Indonesia and Taman Angrek in Jakarta and Mid Valley Megamall, Pavilion KL, Suria KLCC in Kuala Lumpur.</p>
