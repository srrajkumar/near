											<h4 style="text-align:left; margin-bottom: 20px;"><?php echo $title; ?></h4>
											
<p>There are some techniques in the marketing world where success is considered a given. For example, a TV ad slot during the Super Bowl is highly sought after and guarantees high visibility across a multitude of media platforms. </p>

<p>A Super Bowl TV ad has usually resulted in brands securing a top social media spot, most notably trending on Twitter. But this year, the celebration came to an unlikely participant – Esurance stole the show with an ad that launched before the game even started. After months of planning and up to $5 million spent on placements, big brands were left trailing behind as Esurance became the most mentioned company on Twitter.</p>

             							 <p>This story serves as a reminder that there is no one true formula to success. But with accurate data that precisely measures the impact of marketing across various channels, marketers can come dangerously close. </p>
                                         
    <h4 style="text-align:left; font-size:18px;">Are marketers there yet?</h4>                                     
                                         <p>The simple answer? No, they are not.</p>
                                         <p>With nine out of 10 Singaporeans having access to a smartphone and Asia Pacific predicted to chalk up a strong mobile data traffic growth at 54 percent by 2020, mobile has become an indispensable element of everyday life and shows no signs of slowing down. </p>
										 
						 
 <p>Despite this rise in connectivity, marketers still view data for mobile measurement as an optional add-on; a value-add that might get scraped depending on the budget. Many marketers do recognise that the mobile world brings with it a plethora of potential, yet they are still in two minds about tapping into the mobile ecosystem. </p>
 <p>A main reason for this is that many are struggling to get data that is accurate and reliable.  </p>
<p>In today’s data-driven world, with the unparalleled growth in use of mobile and wearable technology, data can be generated from a variety of sources. These large stores of data hold a wealth of information that can be rendered useless unless and until marketers are able to filter the ‘noise’.</p>

<h4 style="text-align:left; font-size:18px;">How can they get there?</h4>
<p>With the rise of cross-channel marketing, mapping the interactions and conversions collected from campaigns that have been caught across multiple channels and screens can be particularly difficult. </p>
<p>Therefore, marketers must ask themselves how can they solve this dilemma and obtain the detailed, accurate attribution data they need. And the answer is simple – mobile location data.</p>
<p>The effect of marketing in the offline world has remained hard to measure. Until now.</p>
<p>Mobile location data is not limited to measuring digital activity – in fact, it is a chain that connects the online world to the offline world. </p>
<p>For instance, in a recent study conducted by Near on <a href="<?php PDF('consumer-intelligence/insights-on-fitness-enthusiasts.pdf'); ?>" target="_blank">Fitness Enthusiasts in Singapore</a>, it was found that males have higher preference for Evolve while females have higher preference for Fitness First. Knowing this, if Nike is looking to promote its new collection of fitness and workout apparels to their target audience (be it females or males), Nike could target users seen in and around Evolve or Fitness First during the particular time period that saw a higher engagement to drive foot traffic to its stores. Furthermore, using mobile location data, Nike would also be able to measure attribution to track if targeted users walked into Nike’s or the competitor’s store instead.</p><br>
<p>By tagging mobile data with location footprints, marketers can follow consumers every step of the way – from initial online browsing to the final in-store purchase. Mind you, this is only the first step towards truly effective attribution and data-driven marketing. </p>
<p>The next is to gain a cohesive understanding of the impact ad campaigns have on every online and offline channel. </p>
<p>Location data has given brands and marketers effective targeting capabilities through mobile. Now, we are seeing location data beginning to improve the relevance of out-of-home (OOH) ads. Marketers are applying the location insights gathered from mobile usage to identify the best OOH locations for their target audience, and even serve dynamic digital creatives.</p>

<h4 style="text-align:left; font-size:18px;">What does the future hold?</h4>
<p>Marketers are definitely moving in the right direction, with improved mobile understanding and technical know-how as compared to just a couple of years ago. That being said, not many have unearthed how they can reach out to the right audience, at the right place with the right media that works the best for them.</p>
<p>The path to purchase is no longer just a linear, one-directional route with predictable outcomes. </p>
<p>Done right, accurate attribution data can help marketers glean insights into consumer media preferences, campaign performance and play a significant role in providing unparalleled insight into the intricacies of the modern path.</p>
<p>As a talisman against the unexpected, an infinite store of knowledge, and a means to ensure consistently strong marketing performance, mobile location data really is the ultimate attribution solution. </p>






